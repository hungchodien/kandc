    <h2>個人情報（エントリー情報及び履歴書、職務経歴書）の取り扱いについて</h2>

    <h3>1．個人情報（エントリー情報及び履歴書、職務経歴書）の利用目的</h3>

    <p>
        当社では、エントリーされた方からお預かりしたエントリー情報、履歴書、職務経歴書など候補者の方に関する個人情報は、候補者の方に適した企業をご紹介するために利用し、他の目的で利用することはございません。</p>

    <h3>2．個人情報（エントリー情報及び履歴書、職務経歴書）の取り扱いについて</h3>

    <p>a） エントリー情報または履歴書、職務経歴書は、エントリー受付担当者及び当社コンサルタント以外が取り扱うことはありません。<br/>
        b） エントリー情報及び履歴書、職務経歴書は施錠保管をし、セキュリティには万全を期しております。<br/>
        c）
        企業への推薦時には、原則として氏名はイニシャルにし、連絡先は伏せた状態で企業に提出致します。内定された際には正式（氏名、連絡先が明示されている）な書類を、ご本人から直接提出して頂きます。但し、セキュリティ対策が講じられている専用WEBサイトを通じて応募書類を提出する等の場合は、氏名、連絡先を伏せずに提出することがあります。<br/>
        d） 登録抹消を依頼された場合、提出頂きました履歴書及び職務経歴書は速やかに破棄いたします。<br/>
        e） 履歴書及び職務経歴書の提出がなされない場合、エントリーに応じることはできかねます。</p>

    <h3>3．個人情報（エントリー情報及び履歴書、職務経歴書）の第三者への提供</h3>

    <p>当社はエントリー情報及び履歴書、職務経歴書を第三者に提供することはいたしません。</p>

    <h3>4．個人情報（エントリー情報及び履歴書、職務経歴書）の外部委託</h3>

    <p>当社はサーバメンテナンス等の業務の一部を委託することがございます。その際、当社は業務委託先との間で個人情報の取り扱いに関する契約を結び、適切な監督を行います。</p>

    <h3>5．個人情報（エントリー情報及び履歴書、職務経歴書）の開示及び訂正、利用停止について</h3>

    <p>エントリーいただいた方が当社に対して、ご自身の情報の開示や削除・利用停止等を求められた場合には、できる限り速やかにご要望に応じるようにいたします。</p>

    <h3>6．掲載の求人、企業情報について</h3>

    <p>
        当社は候補者（エントリーいただいた方）が求人企業に入社した場合の仕事内容や処遇など、労働条件の概略について確認を行いますが、候補者は、候補者の責任において、求人企業に労働条件を直接確認した後に雇用契約を結ぶものとし、当社が確認しかつ候補者に通知した労働条件が、当該雇用契約の詳細を最終的に保証するものではありませんのでご注意ください。</p>

