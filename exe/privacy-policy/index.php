    <h2>個人情報保護方針</h2>

    <p>わたくしたち株式会社クライス＆カンパニーは、当社の人材紹介等のサービスをお客様に安心してご利用頂く為にお預かりする個人情報についての保護方針を策定いたしました。お客様個人を特定する個人情報を取り扱うに際し、全ての役員及び従業員（非常勤社員、派遣社員、パート、アルバイトを含む）がその重大さを認識し、適切な利用・管理を行って参ります。また、業界におけるトップレベルの個人情報保護体制の構築を目指し、以下５つを強化して参りますので、安心して当社サービスをご利用ください。</p>

    <h3>1．個人情報の取得、利用及び提供について</h3>

    <p>
        人材紹介等の事業で取り扱う個人情報及び従業員の個人情報について、適切な取得、利用及び提供を行い、特定した利用目的の達成に必要な範囲を超えて個人情報を取り扱うことはありません。利用目的を超えて個人情報の取り扱いを行う場合には、あらかじめご本人の同意を得ます。</p>

    <h3>2．個人情報に関する法令や指針、規範について</h3>

    <p>個人情報に関する法令・国が定める指針その他の規範を守ります。</p>

    <h3>3．個人情報の安全管理について</h3>

    <p>個人情報への不正アクセスや、個人情報の漏えい、紛失、破壊、改ざん等に対して、合理的な防止並びに是正措置を行います。</p>

    <h3>4．個人情報に関する苦情及び相談について</h3>

    <p>個人情報に関する苦情及び相談には、速やかに対処します。</p>

    <h3>5．個人情報保護の取り組み（個人情報保護マネジメントシステム）について</h3>

    <p>個人情報の保護を適切に行うため、継続的にその取り組みを見直し、改善します。<br/><br/></p>

