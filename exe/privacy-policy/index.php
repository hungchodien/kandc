    <h2>業務運営（転職支援サービス利用）規約および個人情報の取り扱いについて</h2>

    <p>
        株式会社クライス＆カンパニー（以下クライスといいます）が提供する転職支援サービスのご利用にあたっては、下記の利用規約（以下「本規約」といいます）についてご承諾の上でご利用いただけますようお願い致します。</p>

    <h3>1．定義</h3>

    <p>本規約において使用される各用語の定義は、以下に定めるとおりとします。</p>
	
    <p>a）候補者</p>
    <p>クライスに対し、転職支援サービスの利用を申し込んだ方をいいます。</p>
    <p>b）ご紹介企業</p>
    <p>クライスと締結した人材紹介契約に基づき、クライスに対して人材の紹介を委託した企業をいいます。</p>
    <p>c）転職支援サービス</p>
    <p>候補者からお申込いただいたお申し込み内容とご紹介企業からの希望条件との照合、Position
        Information（求人票）および補足する求人情報の提供、転職相談、転職活動支援、応募手続きの代行などのサービスの総称をいいます。
    </p>
    <p>d）クライスの転職支援サイト</p>
    <p>クライスが管理・運営している以下のウェブサイトをいいます。<a href="http://www.kandc.com">www.kandc.com</a></p>
    <p>e）候補者情報</p>
    <p>候補者がクライスに対して提供した、住所、氏名、職務経歴等、候補者に関する全ての情報をいいます。</p>
    
    <h3>2．申し込み方法</h3>

    <p>転職支援サービス利用のお申し込み方法は、クライスが広告またはクライスの転職支援サイトその他において指定した方法によるものとし、その他の方法によるお申し込みはお受けできないものとします。</p>

    <h3>3．サービスの提供</h3>

    <p>
        クライスは候補者すべてに対して、当社からご提供できる求人情報の有無の結果を通知いたします。（既に結果を通知した後の複数回にわたるお問合せに対しては、通知ができない場合もございます。）加えて以下のサービスの中から当該候補者に適切なサービスをクライスの判断で提供するものとします。（クライスの判断によりサービスのご提供ができない場合もございます。）<br/>
        a）電話や面談による転職相談の実施<br/>
        b）担当コンサルタントによる転職活動支援<br/>
        c）Position Information（求人票）を補足する求人情報の提供<br/>
        d）応募手続きの代行<br/>
        e）その他利用者の転職に有益とクライスが判断する一切のサービス</p>

    <h3>4．転職支援サービスの変更・中断・終了</h3>

    <p>
        クライスは、事業運営上やむを得ない場合は、候補者に何ら通知することなく転職支援サービスの全部もしくは一部を変更し、または一時中断することがあります。また、一定期間の通知の上で、転職支援サービスの全部または一部を終了することがあります。</p>

    <h3>5．転職支援サービス提供の終了事由</h3>

    <p>クライスは、候補者において次に掲げる各項に該当する事由が生じた場合は、当該候補者に対して何らの催告を要することなく、転職支援サービスの提供を終了することができるものとします。<br/>
        a）候補者が本規約に定める各事項に違反した場合<br/>
        b）その他、候補者とクライスとの信頼関係が維持できないとクライスが判断した場合</p>

    <h3>6．個人情報の取り扱い</h3>

    <p>クライスは、別途定める「個人情報の取り扱いについて」に従い、候補者の個人情報を適切に収集・利用・提供・管理します。</p>

    <h3>7．候補者情報の正確性</h3>

    <p>
        候補者がクライスに対して提供した情報が正確でなかったこと、および、その内容の不備・齟齬などに起因してご紹介企業、その他の第三者から何らかの異議、請求もしくは要求などがなされた場合には、自己の費用負担と責任で対処するものとし、クライスに一切の迷惑をかけないことを保証するものとします。</p>

    <h3>8．求人照合</h3>

    <p>
        クライスは、候補者からお申込いただいたお申し込み内容とご紹介企業の希望条件とを照合しますが、検討基準や判断理由などをお伝えすることはできません。また、候補者からご紹介企業に対する応募依頼を受け付けて以降も、ご紹介企業からの依頼に基づき、ご紹介企業に代わってクライスが当該求人への適合度合いを判断することがあります。</p>

    <h3>9．労働条件</h3>

    <p>
        クライスは、候補者がご紹介企業に入社した場合の仕事内容や処遇など労働条件の概略について確認を行いますが、候補者は、候補者の責任において、ご紹介企業に労働条件を直接確認した後に雇用契約を結ぶものとし、クライスが確認し、かつ候補者に通知した労働条件が、当該雇用契約の詳細を最終的に保証するものではないことを承諾します。</p>

    <h3>10．禁止事項</h3>

    <p>候補者は、以下の行為をしてはならないものとします。<br/>
        a）虚偽の情報をクライスに提供する行為<br/>
        b）クライスまたはご紹介企業の業務・営業を妨害する行為、または社会的信用もしくは評価を毀損する行為<br/>
        c）他の候補者、ご紹介企業、クライスまたはご紹介企業の従業員その他クライスの事業に関わる一切の関係者<br/>
        （以下「関係者」といいます）を誹謗、中傷もしくは侮辱する行為<br/>
        d）クライスまたは関係者が有する著作権、商標権その他の知的財産権を含む一切の財産的権利、営業上の機密、名誉、プライバシーなどを侵害する行為<br/>
        e）転職支援サービスを通じて入手した情報を、転職支援サービスの目的の範囲を超えて使用し、または第三者に漏洩もしくは開示する行為<br/>
        f）法令もしくは公序良俗に反する行為、またはそのおそれがある行為</p>

    <h3>11．情報の加工</h3>

    <p>
        クライスは、候補者情報のうち、候補者個人を特定できる情報以外の情報を加工し、クライスが編集・発行する各種媒体その他において利用できるものとします。この場合、各種媒体その他で利用された当該情報に関する著作権その他一切の財産的権利は、クライスに帰属します。</p>

    <h3>12．免責</h3>

    <p>クライスは、故意または重大な過失がない限り、転職支援サービスを利用するにあたって利用者に生じた一切の損害（精神的、財産的損害を含む一切の不利益）について責任を負わないものとします。</p>

    <h3>13．不可抗力</h3>

    <p>
        天変地異、ネットワーク上の障害、通常講ずるべきウィルス対策では防止できないウィルス被害その他クライスの責によらない事由によって転職支援サービスの提供が遅延し、または困難もしくは不能となった場合、これによって候補者に発生した一切の損害について、クライスは責任を負わないものとします。</p>

    <h3>14．損害賠償責任</h3>

    <p>候補者が本規約の各事項に違反して、クライス、関係者または第三者に損害を与えた場合は、候補者はその損害を賠償するものとします。</p>

    <h3>15．本規約の変更</h3>

    <p>クライスは、必要に応じて本規約の内容を変更することができるものとします。クライスは、変更後の内容をクライスの転職支援サイト内にて掲載するものとします。</p>

    <h3>16．定めのない事項</h3>

    <p>本規約に定めのない事項は、日本の法令の定めに従い、利用者とクライスとの間において、互いに誠意をもって協議し解決することとします。</p>

