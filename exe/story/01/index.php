         <h3 class="left_content_1 clear">
         <img class="pc_img" src="<?php echo url_root; ?>img/story/vol01_sub_title_02.png" alt="履歴書には明示されていない候補者の能力を見抜き、紹介する" />
          <img class="mobile_img" src="<?php echo url_root; ?>img/story/vol01_sub_title_02_mobile.png" alt="履歴書には明示されていない候補者の能力を見抜き、紹介する" />
         </h3>
         
         <div class="right_content_1 clear">
        		<p>X氏は大手商社を振り出しに金融機関、外資系メーカーを渡り歩いた経歴の持ち主で、米国でMBAを取得してもいます。弊社とは外資系メーカーに転職する際に、お手伝いさせていただき、それ以来のお付き合いでした。<br><br>

ただし年齢は50代。当時は趣味の領域を本格的に学ぶため外資系メーカーを退職し、米国の大学に2年間留学して帰国した直後でした。つまり、履歴書上2年間の空白期間があったのです。非常に優秀でしたが通常の中途採用プロセスを通ると、書類選考の段階で落とされてしまう可能性の高い候補者です。<br><br>

　留学を終えて帰国し、ビジネスの世界に復帰するため、X氏は弊社を訪ねてきてくれました。希望は「経営者の近くで働きたい」。<br><br>

　キャリア面を見ると、X氏は主に営業・マーケティングの世界を歩き、外資系メーカーで人事部門のディレクターとして3年間従事した後、米国本社に招かれて事業開発のディレクターに就任しています。このキャリアを見て「人事のプロ」というには経験が浅いように感じますが、なぜ我々はX氏を推薦しようと考えたのか。
<br><br>
「注目したのは人事部門を3年間担当した後、米国本社に招かれ別の業務に就いていることです。人事での仕事ぶりが本社に高く評価されなければ、こんなことは起こりえません。A社がM&amp;A後の人事的なインテグレーションを実施するにあたって必要なのは、周囲を巻き込む力やプロジェクトの推進力です。その点でXさんは適任と考えました」<br><br>

　当初、A社トップの反応は「人事の経験が少ない」と、あまり乗り気ではありませんでした。しかし「この局面では経営全体に目配りのできる人が必要ではないか」と説得し、X氏との面談が実現したのです。結果として両者は意気投合。それもX氏がM&amp;A後の経営統合に本気で取り組んでいるA社のトップと、経営に関する意見を高いレベルで交わすことができたからです。<br><br>

　そしてX氏は人事部長としてA社に入社し、人事システムの統合や人事評価制度の再構築を手がけM&amp;A後の人事的なインテグレーションを成功に導きました。さらには商品開発や物流拠点網の構築といった業務まで手がけて成果を挙げ、いまではA社の副社長に昇進しています。<br><br>
</p>
        
