         <div class="right_content_1 clear">
        		<p>外資系企業A社の外国人トップと面談したのは、ある大手経営戦略コンサルティングファームのコンサルタントから連絡を受けたのがきっかけでした。<br><br>
「クライアントの外資系企業A社が日本企業M&amp;Aしたが、社内に人事のプロがいなくて困っているので手伝ってくれないか」。そんな相談を受け、まずはA社のトップに話を伺いに行きました。<br><br>
このM&amp;Aは買収された側である日本企業のほうが規模は大きく、かつ企業文化はドメスティックな国内企業と外資系ということでまったく異なっていました。両社の統合がM&amp;A後の大きな課題でしたが、A社には「経営がわかる人事のプロ」がいません。そこでそんな人材を探して欲しいというのが、このコンサルタントからの依頼でした。<br><br>

ちなみに連絡をくれたこの経営戦略コンサルティングファームは弊社のクライアントではありましたが、コンタクトのあったコンサルタントの方とは面識はありませんでした。後日事情を聞くと、ヘッドハンティング会社としての弊社の評判を聞きつけて直接連絡をくれたとのことでした。<br><br>
さっそくA社の外国人トップと会い話をお伺いすると、当初は「経営がわかる人事のプロ」という抽象的な希望にとどまっていましたが、こちらから丁寧に質問を重ねていくうちに、ようやくトップのニーズが具体的に見えてきました。<br><br>
「外資系と日系の両方で働いた経験があり、両者の文化をよく理解している人がよい。日本人の心の機微がわかり、混乱が起こるのが確実な経営統合を一緒にやってくれる、社長の右腕になってくれる人。もちろん英語力は必須で、年齢は40代がよい」

そこで紹介したのがX氏だったのです。実は、A社のトップに会う前からX氏を紹介する腹積もりでいたのです。</p>
        
