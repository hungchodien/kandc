<h4 class="interview_detail_subtitle">マネジャー 公認会計士 堺 敦行氏</h4>

             
             <div class="content-lead">公認会計士として監査業務でキャリアを積み、コンサルタントへの転身を果たした堺敦行氏。現在、かつて産業再生機構でCOOを務めた冨山和彦氏が率いるコンサルティングファーム「経営共創基盤（IGPI）」にて、さまざまな企業の経営支援に奮闘している。会計士の経験は、コンサルタントとしてどう活きているのか、また、堺氏はいまどのようなやりがいを味わっているのか、話をうかがった。</div>
             
