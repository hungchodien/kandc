    <dd>ええ。その頃、仕事の縁でたまたまIGPI代表取締役CEOの冨山和彦氏の話を聞く機会がありました。産業再生機構のトップを務められていた方でしたし、「お上」のような怖い存在なのかと思っていたら、とても気さくで「ユニークなおじさんだな」と（笑）。でも非常に頭が良いことはすぐにわかりましたし、話をすればするほど「世の中にはこんなに凄い人がいるのか」と感嘆させられた。こういう人になりたいと、企業を変えていく戦略コンサルタントに興味を持ったのです。また、ちょうどその後リーマンショックが起こり、世の中がダイナミックに変化するのを目の当たりにして、その渦中に自ら身を置きたいと思ったことも、コンサルタントへの転身を後押ししました。</dd><br>
    
   

