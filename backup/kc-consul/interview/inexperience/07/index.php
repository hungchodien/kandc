            		<dl>
    <dt>入江</dt>
    <dd>経営共創基盤（IGPI）に転職するまでのご経歴を教えてください。</dd>
    <dt>堺</dt>
    <dd>私は2003年10月、大学4年生の秋に会計士試験に合格し、いわゆる４大監査法人のうちの一社に就職しました。そちらでは、主に通信業や製造業などの国内の大手企業の監査を担当し、ベンチャーキャピタル（VC）の監査などにも携わりました。その後、2009年7月にこちらに転職。会計士としてはごく一般的なキャリアのスタートだったように思います。</dd><br>

    <dt>入江</dt>
    <dd>そもそも堺さんは、どうして会計士を目指されたのですか。</dd>
    <dt>堺</dt>
    <dd>もともと会計士になろうと思っていたわけではないんです。実は私、高校生の時から「起業家」を志していました。ちょうどITバブル華やかかりし頃で、ベンチャーの起業家の方々がたくさん活躍されているのを見て、私も強い憧れを持って……大学1年の頃には自分でビジネスプランを考えていたのですが、同時に、やはり世界を見据えなくてはと思い、1ヶ月かけてアメリカ中を旅行しました。そのアメリカ旅行の際、ドル表示だと物価が安く感じて、調子に乗ってクレジットカードで次々と買い物してしまった。帰国後、請求書を見て飛び上がる羽目になり（笑）、結局自分で払えず、親に立て替えてもらうことに。そんな痛い目にあってあらためて考え直したのです。「お金のことが全然わからないのに、起業してもすぐ潰れるな」と。それで会計を学ぼうと一念発起。ですから私の場合、ビジネスをやるために自分の足りない能力を補うために、会計士試験の勉強をしたというのが実情。せっかく試験に合格したので、実務経験を積んで公認会計士として登録されるところまでは会計の仕事をしようと、大手監査法人に入社しました。</dd><br>

    <dt>入江</dt>
    <dd>堺さんのような志向をお持ちの会計士の方は、珍しいのではないでしょうか。</dd>
    <dt>堺</dt>
    <dd>いえ、起業独立を考えている人は案外いると思います。その監査法人の私の同期も、印象では１割ぐらいは「自分でビジネスをやりたい」という志向の人だったと思います。</dd><br>
   
   <dt>入江</dt>
    <dd>監査法人を離れてコンサルタントへのキャリアチェンジをご決断されたのは、ご自身の中で何か契機があったのでしょうか。</dd>
    <dt>堺</dt>
    <dd>冒頭、VCの監査に携わったという話をしましたが、そこでの経験が私にとって大きなターニングポイントになりました。通常の監査は上場企業の経理担当者の方々が相手ですが、VCの監査で接するのはキャピタリストの方々。そこでの議論は、投資先の企業をどう評価するのか、成長性はどうなのか、といったテーマが中心であり、私はまったくついていけませんでした。確かに、監査法人で会計処理についての知識は蓄えられたものの、企業全体を構造的に見るという力はまったく備わっていなかった。それで危機感を覚えるようになって……。</dd><br>
    
    <dt>入江</dt>
    <dd>もともとビジネスをやりたいというお考えだったのに、ご自身のキャリアの中でその力があまり育っていないとお感じになられたのですね。</dd>
