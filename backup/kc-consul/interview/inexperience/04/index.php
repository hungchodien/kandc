    <dd>事業全体を俯瞰し、理解したいとの思いからです。新卒でエンジニア職に就くと、職務上事業全体を見ることが非常に難しく、担当する局所的な開発部位の仕事に留まってしまいます。しかし、お客様の生の反応や事業としての収益性などを踏まえなくてはより価値の高い商品は作れないという危機意識から、事業視点で商品の収益・顧客の顔を見る為に販売の現場に移ろうと考えました。2004年に映像関連機器のマーケティング部門に異動した後、すぐに欧州のマーケティング統括拠点（ロンドン）に赴任しました。現地では20ほどある欧州の販売会社と連携しながら映像関連機器の営業促進の業務に従事しました。具体的な内容としては、当時HD DVDとの規格戦争が起こっていたブルーレイディスク関連機器や、画期的な商品であったロケーションフリーの欧州事業立ち上げ等です。7年半在籍したソニーでは組織は異なっていても、常に一貫してイノベーティブな商品をつくる、あるいは世の中に送り出して市場をつくるという仕事に携わっておりました。</dd><br>

