    <dt>入江</dt>
    <dd>まず、ADL（アーサー・D・リトル）に入社する前のキャリアを教えて頂けますか？ </dd>
    <dt>入口</dt>
    <dd>私は大学院で金属工学を専攻し基礎研究をしておったのですが、研究成果が世の中の商品に活用されるまで10年以上の時間が必要でした。そのため、就職する際には、「自身の取り組む成果・価値がより早く商品になる職種に就きたい」という思いからソニーに入社しました。ソニーでは世界初のHD対応ハードディスクレコーダー用のソフトウェア開発エンジニアとして4年半従事した後、自ら希望して海外マーケティング部門に異動しました。</dd><br>

    <dt>入江</dt>
    <dd>だいぶ畑が異なりますが、なぜ自分から異動を希望されたのですか。</dd>
