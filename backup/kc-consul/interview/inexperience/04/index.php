             <div class="content-lead">1886年、世界初の経営コンサルティングファームとして設立されたADL（アーサー・D・リトル）は経営と技術をつなぐ成長戦略立案や、顧客と共にあるコンサルティングスタイルで知られる。ソニーから転職した入口穂高氏がADLを選んだのは、日本の製造業が競争力を取り戻すための支援を行うのに最適なファームであったからという。</div>
             
