    <dt>大宮</dt>
    <dd>想像以上にきつい仕事でした。自分にとって未知の企業に赴き、そこで10年20年とキャリアを重ねた社員の方々と対等に渡り合うだけではなく、そのような方が感嘆するレベルの付加価値を出していかなければならない。絶えず「考え抜く」ことが求められ、とにかくハード。しかし一方で、このような仕事は我々にしかできないとも感じました。事業会社でプロジェクトに関わる場合、通常業務もあり、ひとつの案件に対して真に「考え抜く」ことは正直難しい。しかし戦略コンサルタントは、その1つのプロジェクトに対して全力でコミットする。事業会社での経験と比較すると、考える時間や深さ、スピード感がまったく違う。そこに戦略コンサルティングファームの、そして自らの存在意義があり、どんな企業のどんな案件であろうと価値を出せるチャンスはあるという自信も生まれてきました。いくつもプロジェクトを経験すると、ある業界で得たノウハウを他の業界に適用することで新たな方向性を見出すなど、自分の引き出しがどんどん増えていき、自らの成長を実感できました。</dd><br>

