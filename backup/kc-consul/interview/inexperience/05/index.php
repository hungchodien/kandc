    <dd>当時、私は最終的に自分でビジネスを興したいと考えていました。そこで、今からすると非常に甘い考えですが、まず「社会人としてのいろは」、そして自分が経営者になったときのために「会社経営のいろは」を学ばないといけないと思っていたんです。加えて日本に閉じこもる発想が好きではなく、世界を見られる仕事がよいと思い、まずは商社に就職しました。</dd><br>

