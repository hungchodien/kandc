    それだけでスキルが向上し、できなかったことができるようになるわけではないのですが、少なくとも自分が思っているほど私のことを他の人は悲観的に見ていないことがわかり、安心しました。意外と自分が思っていた以上にみんな私のことを気にかけてくれていて、BCGの一員として育てようとしてくれている。そんな温かい目で見てくれていることにやっと気付き、いまは辛いかもしれないけれど、BCGの一員としてもう少し頑張ってみようと思いました。</dd><br>

