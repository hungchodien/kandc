    <dt>梶</dt>
    <dd>入社したばかりの頃は、金融という自分の得意分野がある一方で、資料をつくるスピード感や質がクライアントやチームのメンバーが求めている水準になかなか達することができず、そのギャップが苦しい時期がありました。要するに「私、本当はこのくらいできるはずなのに、この会社にいたらこれしかできないなんて」という感じです（笑）。</dd><br>

