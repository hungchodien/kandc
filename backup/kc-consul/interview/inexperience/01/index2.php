    自分が考えていることを率直に発言する重要性はプロジェクトの運営においても同様です。たとえばプロジェクト・リーダーが「私はこう思うからこうしよう」と言ったものの、現場に何回も通っている私たちは「それは適切なやり方ではない」と知っていたとしましょう。そのとき「言っていいのかな…」と躊躇するかもしれませんが、きちんと指摘しなければプロジェクトの価値を下げてしまいます。立場が上の人に「それは間違いです」と言った場合に「いや、絶対に自分のほうが正しい」という人は、BCGにはいませんし、言わないほうが相手に対し失礼にあたります。</dd><br>
      
