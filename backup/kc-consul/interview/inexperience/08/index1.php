    <dd>ご自身は事業会社からコンサルティングファームに移られてきたわけですが、マインドセットやスキルの面で、どのようなギャップをお感じになられましたか。</dd>
    <dt>筒井</dt>
    <dd>越えなければならないハードルがいくつかありました。マインドの面でいえば、「上司と本気で意見を戦わせることができるか」ということ。私はそれまで大企業に9年在籍していましたので、どうしても組織のルールで上役の指示に従って行動する習性が染みついていた。一方で、A.T. カーニーは、役職で遠慮する必要はない、遠慮するぐらいなら会議に出る必要はない、という考え方。意見を言わないことが悪であるというカルチャーなので、それに自分をフィットさせることに少し戸惑いました。<br>
スキルについていえば、2つあります。ひとつは、コンサルタントに求められる「仮説思考」。当初、私は「仮説」という言葉を使うのに抵抗がありました。内心「思いつきと何が違うの？」と訝っていて、「仮説」と言えば何でも許されると思うなよ、と（笑）。でも、実際にこの仮説思考に触れると、私が考えていたレベルとはまったく次元が違うものだった。豊富な知識をベースに、「これは正しいに違いない」と判断したことを積み上げて、論理的に結論を推察していく。ただの思いつきではなく、きわめて高度な思考が求められるのだと気づき、いまは私もこの仮説思考を身につけて案件に臨んでいます。<br>
そしてもうひとつは「伝える力」。自分が手がけたことはすべて報告したいと思いがちですが、ここではそれをやるとまず咎められる。聞く側にとって、全部伝えることが本当に必要なのか？と。「要は何なのか」という本質にフォーカスし、メッセージをできるだけ研ぎ澄まして、いかに早く、わかりやすく伝えられるかが求められる。私たちが接するのは企業の経営層であり、みなさん時間のない方ばかり。過去には、クライアントから「社長が本社から駅まで社長車で移動する10分間で説明してくれ」とオーダーされたこともあります。ですから、伝えることに関して非常に意識するようになりました。この「伝える力」が身につくと、短時間で中身の濃いコミュニケーションができますし、相手の方からも「またこいつと話がしたい」と思っていただける。このスキルはたいへん価値があると思いますね。
