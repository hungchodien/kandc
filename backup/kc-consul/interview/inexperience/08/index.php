             <h3 class="interview_detail_title">A.T. カーニー株式会社</h3>
<h4 class="interview_detail_subtitle">マネージャー　筒井 慎介氏</h4>

             
             <div class="content-lead">大手カード会社に10年弱在籍した後、コンサルタントとして新たなチャレンジがしたいとA.T. カーニーに転職した筒井慎介氏。彼を決意させたのは、日本のための力になりたいという思いと、そして自らの成長に対する危機感だった。筒井氏はなぜA.T. カーニーを選び、そしていま、A.T. カーニーでどんなキャリアを手に入れているのだろうか。</div>
             
            	<div class="clear green b content_title_view">かつての日本の勢いを、取り戻す力になりたい。</div>
            		<dl>
    <dt>入江</dt>
    <dd>A.T. カーニーに転職されるまでのご経歴を教えていただけますか。</dd>
    <dt>筒井</dt>
    <dd>私は2000年に大手カード会社のJCBに新卒で入社しました。最初に配属になったのは情報ネットワーク部で、当時ちょうどクレジットカードがICカードに切り替わるタイミングであり、それを推進していく業務に5年ほど携わりました。この間、VISA やMasterCardなど海外のカード会社と共通化を協議したり、あるいはJCBの海外における取引先へのICカード対応などにあたるため、よく海外にも赴いていました。その後、部署を異動して電子マネーの立ち上げに関わることに。セブン＆アイホールディングスが展開する電子マネーの“nanaco”の開発をJCBは野村総合研究所とともに担ったのですが、そのプロジェクトに参画しました。2年半ほどそこに関わった後、今度はまったく畑違いの人事に異動。社内の人事制度改革プロジェクトにアサインされ、新たな人事制度の構築を1年ほど手がけた後、2009年にA.T. カーニーに転職したという次第です。</dd><br>

    <dt>入江</dt>
    <dd>そもそもどうしてJCBを就職先として選ばれたのでしょう。</dd>
    <dt>筒井</dt>
    <dd>実は私は理系出身で、もともとはテクノロジー志向でした。しかし、研究職のような技術だけを究めていくキャリアには興味が持てなかった。そこにはビジネスの匂いが感じられず、私としては「技術を市場でどう活かすのか？」というレベルまで踏み込みたいと考えていました。それで、社会に広く影響を及ぼせるコンサルタントに興味を覚えたのですが、当時の私には得体のしれない世界で、将来コンサルタントの道を進むにしても、まずは実務を経験しておいたほうがいいのでは、と。JCBを選んだのは、決済サービスの可能性に惹かれたから。通信技術の進歩により、これから新たな決済サービスが普及して世の中が変わっていくと感じ、そこにテクノロジーをもって関わりたいとJCBを志望しました。最終的な決め手となったのは、これはA.T. カーニーに転職した時も同様だったのですが、「この人と一緒に仕事がしたい」と思える方が多かったことですね。</dd><br>

    <dt>入江</dt>
    <dd>もともとコンサルタントには興味をお持ちだったのですね。ご自身がA.T. カーニーに転職された時は、もう30歳を超えていらっしゃったかと思いますが、そのタイミングでコンサルタントへの転身を決断されたのは、何か契機がおありだったのでしょうか。</dd>
    <dt>筒井</dt>
    <dd>結果としてJCBには9年在籍したことになりますが、いまとなって思えば、もう少し早く決断してもよかったかもしれません。実は、最初の部門でICカード化推進に携わっていた時から、コンサルタントへの転身を少し意識し始めていました。先ほどお話ししました通り、当時は海外に出張する機会が多かったのですが、2000年代初頭の頃は空港に着いてタクシーでホテルに向かう道中、車窓から目に入ってくるのは日本のメーカーの看板ばかりでした。それが2000年代の半ばになると、日本企業の看板がめっきり減り、代わって韓国のメーカーの看板が目につくようになってきた。私はそれまでナショナリズムなど特に意識したことはなかったのですが、ここまで日本企業の凋落を目の当たりにすると、かつての勢いを取り戻したいという思いがおのずと湧いてきたのです。そして、その力になれるのは、やはりコンサルタントではないかと。<br>
が、その後忙しい日々を送っているうちに、その時抱いた感慨も薄れてきて、キャリアを積むと社内での居心地もよくなり、知らず知らずのうちにそうした状況に満足してしまっていました。そして30歳を超えた時、ふと我を省みると「このままでいいのか」という危機感が……居心地が良いというのは、つまり、仕事を進めるうえで壁を感じていないということ。壁にぶつからなければ、成長のきっかけもない。自分の社内価値は上がっているかもしれないが、市場価値は停滞しているのではないか？　いま置かれている環境で、私の裁量が急に大きくなるわけでもない。そう思うと急に焦燥感が募ってきて、この歳で行動を起こさないともう外には出られないかもしれないと考え、コンサルタントへの転身を決意したのです。
</dd><br>
   
 
   

