    <dd>かつての私のように、コンサルティングという仕事は好きだけれども、現状に“不完全燃焼”感を抱いていて、それを払拭したいという気概のある方に参加していただきたいですね。自分の能力に自信があって、たとえば経営者としての経験を積んでみたいという方も大いに歓迎します。才能ある若い人材が、年功序列でやりたいことができないというのは本当に不毛なこと。先に触れた、ベンチャーキャピタルやPEファンドの会社を社内で立ち上げたのも、自ら意思決定して事業を動かす経験をコンサルタントたちに提供するためです。そのような経験をした年月は、コンサルティングばかり手がけるよりも、100倍実りある経験になる。日本においては貴重な存在であるハイブリッドなコンサルタントが養成されることになり、それが結果として日本のコンサルティングを変えていくことにつながっていくのだと私は捉えています。</dd><br>

