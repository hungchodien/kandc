            		<dl>
    <dt>入江</dt>
    <dd>並木さんが掲げる「企業を対象とせず、人を対象とする」コンサルティングというのは、どうすれば実現できるのでしょうか。</dd>
    <dt>並木</dt>
    <dd>日本のコンサルティング業界の手法を見直していくことで、進むべき新たな道を見出せるのではないかと思っています。従来のように、短期的・単発的なプロジェクト単位で契約を結ぶ形では、時間軸の問題として「人」を支えるコンサルティングは成立しません。長期間に渡ってサポートできる関係性を持てるような契約の形態を模索しなければならないでしょう。大手のコンサルティングファームでは、100年以上の歴史で積み重ねてきた手法が確立されており、それを根本から変えることは極めて難しい。そこで、私が設立したフィールドマネージメントでは、リスクとリターンをクライアントと共有していこうという発想のもと、あえて成功報酬型の契約形態を試したりもしています。そうした姿勢で臨むことが、私たちが導き出した第一の解です。</dd><br>

    <dt>入江</dt>
    <dd>なるほど、長期間に渡ってクライアントとパートナーシップを結べるモデルを追求しているわけですね。</dd>
    <dt>並木</dt>
    <dd>そしてもうひとつ、これは日本においてのコンサルティングが欧米に比べて深く浸透しない理由のひとつでもあるのですが、日本の社会では豊富な実務経験を積んだ“グレイヘア”の人材の助言のほうが尊重される文化があります。欧米では、優れた分析力をもった“ファクトベース”のコンサルタントも、グレイヘアと同等な評価を受けています。では、この日本市場で、これまでファクトベースでのコンサルティングを手がけてきた私たちが、グレイヘアの人材に匹敵する信頼と評価を得るためにはどうすればいいのか。そのためには、やはり実際に起業したり経営に携わったりする経験を若いうちから積むことで、ファクトベースの能力に加え、グレイヘアとしての実践的な視点や経験をもつ、いわば“ハイブリッド”なコンサルタントを育てることが有効だと考えています。そこでフィールドマネージメントでは、社内でそうした能力を手に入れられる仕組みを作り、年配の経営者が思わず耳を傾けるようなコンサルタントの育成に取り組んでいます。</dd>
 <br>

    <dt>永田</dt>
    <dd>グレイヘアの人材に匹敵する能力を手に入れる仕組みとは、具体的にはどのようなものですか。</dd>
    <dt>並木</dt>
    <dd>フィールドマネジーメントでは、コンサルティングサービスを展開するかたわら、自社でベンチャーキャピタルやPEファンドを立ち上げ、当社のコンサルタントたちが経営を担ったり、あるいはキャピタリストやファンドマネージャーとして力をふるっています。こうした経験を積める場を提供することで、グレイヘアの能力を身につけられると考えています。
</dd><br>

 <dt>永田</dt>
