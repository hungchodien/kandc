    <dt>並木</dt>
    <dd>その通りです。私たちの想いとしては、日本のコンサルティング業界を変えていきたい。そして、そうした志に賛同し、応援してくださる経営者の方も徐々に増えています。<a href="http://cms2x.wired.jp/2014/02/10/jal-yoshiharu-ueki-vol10/" target="_blank" rel="nofollow">『WIRED』VOL.10</a>のインタビューで私たちの姿勢をご評価くださっているJALの植木社長もそうです。あるプロジェクトのコンペで、大手のコンサルティングファームではなく、フィールドマネージメントをパートナーとして選んでくださりました。それは、当社が“ファクトベース”と“グレイヘア”がハイブリッドされたコンサルティングファームであり、事業を創ることの大変さを肌で理解している人材を擁することを評価いただいたとともに、本気で日本のコンサルティングを変えようとしている、私たちのような若手に未来を託したいというメッセージも込めていただいたのではないかと思っています。フィールドマネージメントを設立して6年が経ちましたが、私たちが掲げるビジョンの実現に大きな手応えを感じており、自信が確信に変わってきました。
</dd><br>



   
