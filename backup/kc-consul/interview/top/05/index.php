             <h3 class="interview_detail_title">株式会社フィールドマネージメント</h3>
<h4 class="interview_detail_subtitle">代表取締役　並木 裕太氏</h4>

             <div class="content-lead">マッキンゼーにおいて最年少で役員に昇格し、コンサルタントとして大きな実績を誇る並木氏が、2009年に自ら立ち上げたファームがフィールドマネージメントだ。並木氏が独立した背景には、日本のコンサルティング業界を変えていきたいという強い意志がある。なぜそうした思いに至ったのか、そしてこれから何を目指していくのか、並木氏に話をうかがった。</div>
            	<div class="clear green b content_title_view">「企業を対象とせず、人を対象とする」コンサルティングを追求したい。</div>
            		<dl>
    <dt>入江</dt>
    <dd>まずは、並木さんがマッキンゼーから独立されて、このフィールドマネージメントを立ち上げた経緯を教えていただけますか。</dd>
    <dt>並木</dt>
    <dd>私はマッキンゼーで約9年間、コンサルタントとしてのキャリアを重ねましたが、その仕事のすべてに満足していたわけではありませんでした。コンサルタントとしての力量が足りなかったこともあるのでしょうが、クライアントに心の底から「ありがとう」と言っていただけたプロジェクトは決して多くなかったように思います。おそらくそれは、私だけに限らず、日本のコンサルティング業界全般に当てはまることではないでしょうか。クライアントとコンサルタントがともに満足できるようなコンサルティングのあり方とはどのようなものなのか、また自らが主体となって業界に変化をもたらすことはできないか。そうしたことを少しずつ考え始めるようになったのが、独立の道を選ぶきっかけになりました。
</dd><br>

    

    <dt>入江</dt>
    <dd>なぜ、日本のコンサルタントはクライアントから感謝される機会が少ないのでしょう？</dd>
    <dt>並木</dt>
    <dd>まず、日本ではコンサルティングファームの社会的地位が欧米に比べて低いという点が挙げられます。欧米では大企業の役員がコンサルタントを仕事のパートナーとすることは当然のように行われていて、コンサルタントという後ろ盾を持つことで社内での意思決定がスムーズに進められるというメリットもあります。しかし日本の企業にはそこまでの土壌があるとは言えません。<br>
また、コンサルティグの対象が「企業」であるという点にも問題意識をもっています。たとえば、あるクライアントの経営陣の一人と信頼関係を築いていたとしましょう。コンサルティングファームとしては、次期社長候補と見込んで付き合っている面もあるわけです。ところが、パワーバランスが変わり、次のトップは別の人になりそうだと判明する。「企業」を支えることを主眼とするコンサルティングファームの判断に従えば、これまで付き合ってきた方だけでなく、次期社長とされる人とも関係構築を行うことになります。ドライな欧米ではそれでも通用するかもしれませんが、日本企業の風土にはなじまないだけでなく、むしろ信用を失うことにもつながってしまいます。<br>
さらに、コンサルタントが解決を求められている「課題」そのものを疑う視線も重要です。企業から提示された課題を鵜呑みにして、ひたすらその解決に力を注いだとしても、問題設定自体が間違っていたら意味のないものになってしまいかねません。クライアントの要求に一生懸命応えたにもかかわらず、何か足りない感覚が残り、心からの感謝を得られない大きな理由がそこにあると思います。<br>
私自身はコンサルティングが好きで、本当に価値のある仕事だと思っていますので、やるからにはお客様から喜んでいただけるような仕事がしたい。ならば自分でコンサルティングファームを立ち上げようと決意しました。<br>
</dd><br>

 <dt>永田</dt>
    <dd>では、クライアントから真に感謝されるために、どんなコンサルティングをやるべきだと並木さんはお考えになられたのですか。</dd>
    <dt>並木</dt>
    <dd>私が志したのは、「企業を対象とせず、人を対象とする」コンサルティングをしようということです。大きなビジョンに向けて努力されている経営者の方々、自分が本当にお付き合いしたいと思う方々に寄り添って、誰よりも近い存在になる。先ほど申し上げたような、「企業」を対象とすることで起こる“ねじれ”のようなものも、対象を「人」にすれば解消されるはずです。その経営者の方と心中するぐらいの気持ちで、たとえ転職して今とは異なるフィールドに移ったとしてもサポートしつづけ、引退の仕方まで一緒に考える。そういう関係性でのコンサルティングを追求したほうが、より感謝される仕事ができるでしょうし、私自身もピュアに取り組めると思ったのです。そして「人」について長く一緒にいると、その方が本当に大切にしていること、人生を通して本当にやりたいことがおのずと見えてきますから、問題設定を誤るということもなくなってくる。こうしたコンサルティングが実現できれば、これまで私が抱えていた悩みを解消できるのではないかと考えたわけです。
</dd>
   
   
    </dl>
