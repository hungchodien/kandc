            		<dl>
    <dt>入江</dt>
    <dd>御社のコンサルティングスタイルに何か特徴はありますか。</dd>
    <dt>塚原</dt>
    <dd>やはり戦略からITまで一貫してシームレスにコンサルティングできることに、我々の存在意義があると思っています。求められる戦略を立案し、それに即した組織や業務、物流、システムをトータルに再構築していく。その際、クライアント側に知見を持つ人材がいなければ、カート・サーモンの人間が現場に入ってPDCAを回していく。クライアント企業の名刺を持って1年2年と先方に在籍することも珍しくありません。さまざまな領域の専門家がワンチームとなり、戦略を描いて計画を立てることだけに留まらず、真に実行されるところまで徹底的に関与していくことが、我々のスタイルです。</dd>
    <dt>河合</dt>
    <dd>昨今、「成果が見えるところまでコミットする」というタンジブルリザルトを謳っているファームが多いようですが、結局、パートナー、マネージャー、アナリストがチームを組んでサービスにあたるというモデルはどこも変わらず、あまり進歩は見られません。おそらく今後は、クライアントに直接的なインパクトを与えられるサービスを開発しなければ、コンサルティングというモデルは成り立たなくなっていくでしょう。一方でカート・サーモンは、ひとつのインダストリーに特化しながら繰り返しノウハウを蓄積し、イシュードリブンでトップからボトムまでシームレスに手がけています。こうした業態に、私はコンサルティングの未来像を感じるのです。手前味噌かもしれませんが、コンサルティングファームが進化した先にあるのが、カート・サーモンなのではないかと。</dd><br>

    <dt>永田</dt>
    <dd>なるほど、コンサルティングファームの進化形だという話は大いに説得力があります。では今後さらに事業を拡大していくために、御社はどのような戦略を描いていらっしゃるのでしょうか。</dd>
    <dt>塚原</dt>
    <dd>我々はこれまで小売業にフォーカスし、きわめて高いスペシャリティを発揮してきましたが、ここで培われた知見をもとにコンサルティングサービスを展開できる分野は他にもあると考えています。たとえば消費財メーカーのクライアントに対しても、我々は大いに貢献できると考えています。
</dd>
 <dt>河合</dt>
    <dd>そう、我々が持っているノウハウは日本の製造業にも求められるものだと思っています。これまで日本の製造業は優れたモノづくりの技術で成り立ってきましたが、いまや良い製品を作れば売れるという時代ではありません。重要なのは、マーケティングなどのセルサイドのテクノロジー。それを究めているのは、やはりリテールの小売業です。国内の消費財メーカーが小売業に学ばなければならないことは、実はたくさんあるのではないでしょうか。
</dd>
<dt>塚原</dt>
    <dd>たとえば「缶コーヒー」などは顕著な例ですね。これまで飲料メーカーの技術者が懸命に研究開発に取り組み、本物の味や香りを追求していきましたが、コンビニエンスストアが淹れ立てのコーヒーを廉価で提供するようになると、そちらにお客様を一気に奪われてしまいました。プロダクトの開発力も確かに重要ですが、それ以上に商品のマーケティング力が求められている時代なのです。さまざまなプレイヤーが入り乱れて競争が激化し、いっそう薄利になっているマーケットで戦っていくためには、メーカーもチャネル戦略やリベート戦略などに真剣に取り組んでいかなければならない。そして、そうしたテーマはまさに我々が得意とするところです。
</dd><br>

    <dt>入江</dt>
    <dd>つまり、御社が培ってきた小売業に関するノウハウは、製造業にとってもこれから重要なノウハウになる、と。</dd>
    <dt>塚原</dt>
    <dd>その通りです。すでに消費財メーカーからもいくつかオファーをいただいていますし、その領域にもサービスを拡げていきます。また、今後は日本の小売業のグローバル展開も支援していきたいと考えています。小売業の海外進出はなかなか難しく、過去失敗した例がいくつも見受けられますが、そんななかで勝ちパターンも出てきています。特にアジア市場に向けて再挑戦したいという企業も現れており、我々もその力になっていきたい。現在、アジアにおける拠点は上海だけですが、それをこれから増やしていく方針です。
</dd><br>

 
   
