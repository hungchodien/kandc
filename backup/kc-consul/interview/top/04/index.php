            		<dl>
    <dt>永田</dt>
    <dd>まずは塚原さんに、コンサルティングファームとしてのカート・サーモンの特徴を具体的におうかがいしたいのですが。</dd>
    <dt>塚原</dt>
    <dd>カート・サーモンはグローバルに事業を展開するファームですが、なかでも日本においては「リテール」に特化したコンサルティングサービスを提供しています。小売業というのは、店舗という現場において従業員と消費者が直に接する産業です。そこで我々コンサルタントに求められるのは、考えたプランを真に現場で実現できるかということ。頭でっかちで企画倒れに終わるようなコンサルティングでは意味はありません。しかし、小売というのは実は非常に難しいビジネスで、とても一筋縄ではいかない。物流ひとつとってもそう。ある会社でのSCM構築に携わった経験やそこで得たナレッジが活かせるかと思いきや、まったく通用しないです。小売業はBtoBの領域よりも需要の読みが難しく、まず計画通りにはいかない。全体を網羅するサプライチェーンやロジスティクスを構築するのはきわめて困難。また、商品戦略についても、地域の特性によって売れ筋商品は異なるため、現場ごとに戦略をカスタマイズして実行に移していかなければなりません。その指南役を担うのが我々なのです。
</dd><br>

    

    <dt>永田</dt>
