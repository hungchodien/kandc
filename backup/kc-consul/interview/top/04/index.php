 <dt>入江</dt>
    <dd>河合さんは、カート・サーモンの優位性はどこにあるとお考えですか。</dd>
    <dt>河合</dt>
    <dd>コンサルタントというのは、その業界のことを知らなくてもクライアントにアドバイスできる、などとまことしやかに言われています。確かにこれまでは、フレームワーク思考などを駆使すれば、インダストリーの専門性がなくても課題を解決できた。しかし、徐々にそれが通用しなくなってきている。フレームワーク思考などのナレッジは誰もが容易に入手できるほどオープンになっていますし、加えてそれぞれのインダストリーが抱える要件というのがいっそう複雑になっています。もはや優秀な人材が3日間でキャッチアップして戦略を語れる、という次元ではない。その点、我々は小売業界に精通しているので、最初からクライアントと同じ知識レベルで議論し、本質的な課題をすぐに抽出することができる。しかも、同じインダストリーに繰り返し関わっていくことで、この業界特有のノウハウが蓄積され、どの案件にもアナロジーで展開できます。
</dd>

 <dt>入江</dt>
