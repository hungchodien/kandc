             <div class="content-lead">人々の生活に身近な「小売業」に特化し、そこでクライアントに深く入り込み、さまざまな経営課題を実際に現場で解決するところまで関わっていくという、コンサルティングファームとしては際立った特徴を見せるカート・サーモン。同社の思想や今後のビジョン、さらにはここでキャリアを積む意義について、パートナーのお二人に話をうかがった。</div>
