    <dt>前田</dt>
    <dd>NRIで太陽光発電事業に関わっていた時、そこで業界内で流動性がまったくないことに気づき、強く問題意識を抱くようになりました。たとえば川上の素材やモジュールのメーカーは、川下のシステムのことやファイナンスのことにまったく通じていない。太陽電池とモニタリングのシステムを組み合わせれば、最適な発電ができる優れたソリューションが実現できるのに、川上と川下の間に人材の流動性がないのでそれが果たせない。結局、単に太陽電池単体を売るだけで付加価値が出ない状況に陥っている。次第に、これは太陽光発電業界だけの課題ではなく、日本の産業全体が抱える課題ではないかと感じるようになったのです。</dd><br>

