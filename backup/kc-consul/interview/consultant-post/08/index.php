<h4 class="interview_detail_subtitle">代表取締役CEO＆ファウンダー　前田 佳宏氏</h4>

             <div class="content-lead">大手企業と中小企業の“モノづくり”のマッチングを図るサービス“Linkers（リンカーズ）”を運営するリンカーズ株式会社。日本の産業を変える可能性を大いに秘めたこの企業を立ち上げたのが、コンサルタント出身の前田氏だ。過去の経歴と起業に至った思い、そして目指す姿などについて話をうかがった。</div>
            	<div class="clear green b content_title_view">「戦略」だけでは事業は動かない。意味のある仕事がしたい。</div>
            		<dl>
    <dt>入江</dt>
    <dd>前田さんの経歴を拝見させていただくと、大阪大学工学部を卒業後、新卒で京セラにご入社されています。なぜ京セラを就職先として選ばれたのですか。</dd>
    <dt>前田</dt>
    <dd>大学時代、私は経営者の書いた本を読むのが好きで、なかでも京セラの稲盛和夫名誉会長の「成功への情熱」にたいへん感銘を受けました。現在に至るまでもうかれこれ5回は読んでいますが、私はどちらかというと「精神論」に惹かれるほうで、稲盛さんの言葉に私はとても共鳴し、ぜひ京セラで仕事がしたいと思ったのです。京セラに入社後は海外営業部門に配属となり、韓国メーカーを担当。毎週のように韓国に出張し、携帯電話の部品などの営業に奮闘しました。</dd><br>

    

    <dt>入江</dt>
    <dd>その後、前田さんは野村総合研究所（NRI）に転職されています。転職を決断されたのは、何か契機があったのでしょうか。</dd>
    <dt>前田</dt>
    <dd>京セラで海外営業に携わるうちに、徐々に自分が担っている海外営業という機能は必要ないのではないかと感じるようになりました。現地に日本語が話せる人間がいて、工場とダイレクトにやりとりすれば問題ない。海外営業が介在することで、かえって情報が曲げられたり劣化したりする。業務に慣れるにつれて自分の役割に疑問を抱くようになり、それで転職を考えるようになりました。もともと私は社会に出た時から「世の中に大きなインパクトを与えたい」という思いを抱いていて、それがかなう場として、事業戦略を練って企業を変革していくコンサルティングファームに興味を持ちました。なかでもNRIを選んだのは、お会いした方がみなさん非常に優秀だったことと、やはり国内では知名度が高く、業種を問わずいろいろな企業に関われると考えたからです。</dd><br>

 <dt>入江</dt>
    <dd>NRIでは、どのような案件を手がけられたのですか。</dd>
    <dt>前田</dt>
    <dd>NRIには6年ほど在籍し、最初の4年間は主に製造業の事業戦略の策定に関わりましたが、そこで得た結論は、戦略を練ることはできても、それを実現するのはきわめて難しいということ。NRIでは“GISOV（Goal／Issue／Solution／Operation／Value）”というフレームワークでコンサルティングを展開していましたが、“GIS”、すなわちゴール設定～課題発掘～解決策立案までは戦略コンサルタントが一人で担えるものの、“O”、つまり事業化の段になり、ステークホルダーが絡むと難度が一気に上がる。結局、企業にはそれぞれルールがあり、ギブ・アンド・テイクのバランスが取れないと人が動かないということをあらためて認識しました。</dd>

 <dt>入江</dt>
    <dd>戦略コンサルタントが果たす役割に限界を感じられたということなのでしょうか。</dd>
    <dt>前田</dt>
    <dd>これは私見ですが、クライアントがコンサルタントを使うのは、上層部を説得するための材料集めのために過ぎないのではないかと。結局、コンサルタントが価値を提供できるのは「市場調査」であって「事業戦略」ではない。4年間でそう見切って、私としてはやはり意味のあることをやりたいと、会社に希望を出して太陽光発電事業に関する買収を専門に手がけることにしました。ちょうどエネルギー業界が変革するタイミングで社会的に意義があると感じましたし、やるからには川上の素材から川下のシステムエンジニアリングまで一貫して関わって、きちんと事業化するところまでコミットしたい。なかでも私が手がけたかったのはM&amp;A。企業を買収すると必ず“O”が絡んでくるので、本当に社会に対して価値のあることを実現できると考えたのです。この太陽光発電事業のコンサルティング案件では、市場調査から始まってM&amp;Aの戦略立案、そしてファイナンスからトランザクションまで、すべてを担いました。
</dd>
   
   
