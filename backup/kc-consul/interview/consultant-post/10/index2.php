    <dd>田村さんが、経営者としてのポジションを獲得できた一番の要因は何だとお考えですか。</dd>
    <dt>田村</dt>
    <dd>それはやはり企業再生支援機構での経験が大きいと思います。機構では、私のようなコンサル出身者はもとより、投資会社出身者、会計士・弁護士、M&amp;Aアドバイザリーなど、実に多様な専門家の混成部隊が、事業会社の役職員はもちろん、あらゆるステークホルダーと、あるときは協力し、あるときは対峙しながら、七転八倒しています。大変革期にある日本企業のプロ経営者に求められる資質に気づき、自ら現場で事業をターンアラウンドする経験を積み、そして人の縁も得たからこそ、JVCケンウッドに経営者として参画できたのだと思います。</dd><br>

    <dt>入江</dt>
    <dd>では最後に、事業を経営する側で次のキャリアを考えているコンサルタントの方々にアドバイスをお願いします。</dd>
    <dt>田村</dt>
    <dd>戦略コンサルタントとして得られるスキルで事業会社においても重要なのは、先ほども触れました通り、物事の考えぬく力だと思います。ただ、その方向性を誤ってはいけません。たとえば “A”という初期仮説を立てたとして、ダメなコンサルタントは、“A”の検証に全精力を傾けてしまうんですね。ちょっと気の利いたコンサルタントは、敢えて“A”を否定してみることで“A”を“B”に進化させようとします。優秀なコンサルタントは、そもそも“AかBか”は課題の本質ではなくて･･･、と更に思考を深めていきます。 “A”を主張する自分、“A”を否定する自分、課題設定そのものを見直す自分。私はよく「3人の自分をもて」と表現するのですが、そうした思考を養っておくべきだと思います。</dd><br>

    <dt>入江</dt>
    <dd>そうした思考は、事業会社に身を置いた時にどのように役立つのでしょうか？</dd>
    <dt>田村</dt>
    <dd>社員はみなさまざまな思いを抱いています。誤解を恐れずにいえば、事業会社は個別最適の塊なんです。「正しい」「正しくない」で割り切ろうとしてもなかなかそうはいかない。戦略は常に構想通りには進まない。その際、自分が「正しい」と考えることを理路整然と主張するだけではまず通用しない。求められるのは、対立の奥にある本質を見極めて、対立そのものを無意味にしていく力。根本を解きほぐして、それに関わる全員が同じ方向を向けるように仕向けていく力。そこに戦略コンサルタントの思考法が大いに役立つと思いますし、事業会社でそれができる人はあまりいないように見受けられます。加えて、事業会社では、その企業が長い歴史の中で培ってきた文化や風土を心底理解しなければいけない。ベンチマーキングに裏付けられた「べき論」など、絶対に完遂できません。パートナー時代、ある歴史ある大企業の副社長から「提案いただいた新事業は魅力的だと思う。でもうちがやる事業ではない」と言われたことがあります。事業会社の経営というのはそういう側面もあるということをきちんと心に留めておくべきだと思います。</dd><br>
    
