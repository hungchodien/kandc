            		<dl>
    <dt>入江</dt>
    <dd>経営者になるためには、ファームのパートナー経験はそれほど必要ない、ということですか？</dd>
    <dt>田村</dt>
    <dd>個人的にはそう思いますね。事業会社で生きていくための力を得るには、プロジェクトマネージャーを経験すれば十分ではないかと。コンサルタントは、短期間で大まかな答えを示す仕事。一方で、事業を担うということは、最後の1％まで自らやりきらなければならない。根本的に求められる資質が違うんですね。だからプロフェッショナルファーム業界を究めたところで、その経験が事業会社で役立つかといえば、意外とそうではない。と言いながらも、私がアクセンチュアでパートナーを務めていた時は、下のメンバーに対して「パートナーになってこそ一人前だ」と指導していたのですが（笑）……でもそれは、ファームに在籍する以上は、その世界でトップにたつという志を持つべきだというのが私の考えで、そうでなければ、壁にぶつかた時に逃避してしまう。成果を上げられず認められないから転職する、というのは私の価値観にはなかったものですから。</dd><br>

    <dt>入江</dt>
    <dd>いま田村さんは、「事業会社で生きていくための力は、マネージャーを務めれば十分得られる」とおっしゃられましたが、それは具体的にはどんな力なのでしょう。</dd>
    <dt>田村</dt>
    <dd>ビジネスを動かす上で必要とされるのは、物事を徹底的に考えて本質を見極める力です。それはプロジェクトマネージャーになるまでに十分身につくものだと思っています。一方で、いくらコンサルタントとしてキャリアを積んでも得られないものもある。それは、キャッシュの重みを感じながら事業に関わるという経験。確かにコンサルタントは、さまざまな経営者の考え方に触れる機会には恵まれますが、排水の陣でそれを実践することはない。事業会社で重要なのは、やはりそこなんですね。本来ならば、経営者へのパスと考えると、私も30代でそんな経験を積むべきだったと感じています。</dd><br>

    <dt>入江</dt>
    <dd>やはり経営者を志しているコンサルタントは、若いうちにアクションを起こしたほうがいいということですか？</dd>
    <dt>田村</dt>
    <dd>将来、社会に対して及ぼす影響力の大きな、エスタブリッシュな日本の大企業の経営に携わりたいと考えるなら、やはり30代からそうした経験を積んでおかないと間に合わないように思いますね。30代で、小さくてもいいので自ら収益責任を負って何かの商品やサービスを扱う経験を重ね、次第に大きな塊を自分で動かせるようになってはじめて、多くの社員の家族の生活を背負う資格が得られるのだと思います。このステップはどうしても必要。実際、学卒でファームに入り、そのまま純粋に戦略コンサルタントして経験を積み、40歳50歳になってパートナーからいきなり大企業の経営者に収まるというのは、例があまりありません。退路をたって、キャッシュの重みを感じながら事業運営する経験を積んでいないからなんですね。おそらく経営者をスカウトするヘッドハンターも、コンサル経験しかない人材は敬遠するんじゃないでしょうか。私は運良くファームでパートナーまで務めて経営者になりましたが、こうした経歴のほうがレアだと思いますね。</dd>
    
    
    
    
