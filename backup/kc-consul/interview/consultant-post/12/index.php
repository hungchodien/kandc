    <dd>社会に出て実際にエンジニアの仕事をする中で、自分は「自然科学」より「社会科学」のほうが好きなんだなと気づいたんです。エンジニアというのは自然科学の領域で、閉じた世界で論理を突き詰めていく仕事。それはそれで面白かったのですが、実際の社会というのは色々なステークホルダーが存在して複雑に絡み合い、単に優れたものを創ったからといって世の中で使われるようになるわけではない。ごく当たり前の事実なのですが、実際に働く中で改めてそのことを認識させられました。それで、世の中のさまざまな複雑な要素をうまくドライブしながら、最終的な変化の実現に繋げていく社会科学的な仕事の領域のほうが面白いのではないかと感じるようになり、自分の立ち位置を技術側から事業側に移そうと思うようになりました。そうしたキャリアチェンジの第一歩として、クロスインダストリーで多種多様な経営アジェンダに関われる戦略コンサルティングという仕事があることを知り、移籍しました。</dd><br />
    
