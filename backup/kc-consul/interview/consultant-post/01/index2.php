             <p>シティック・キャピタル・パートナーズ・ジャパン・リミテッド</p>
<p>日本代表　中野 宏信氏</p><br><br>

            	<div class="clear green b content_title_view">「経営は怖い！」</div>
            		<dl>
    <dt>丸山</dt>
    <dd>アドバンテッジに入られた頃に中野さんとお会いしたとき、「経営者の案件が出るかもしれない」というお話をいただきましたが、最終的に投資先の富士機工電子にご自身がトップとして行かれましたね。</dd>
    <dt>中野</dt>
    <dd>富士機工電子は会社更生法を申請した会社で、当時はファンドがはじめて法的整理を行った会社のスポンサーになった案件として注目されました。<em style="font-weight:bold; font-size:14px;">社長ができる人を探したのですがなかなか見つからず、自分が社長になりました。</em>流れで経営者になったわけですが、よく考えるとこれは自分がトヨタにいた頃からやりたかったことなんですよね。</dd><br>

    <dt>丸山</dt>
    <dd>ご自身の思いがいろいろなものを決断の場面に引き寄せたんですね。実際、経営者になったご感想はいかがでしたか。</dd>
    <dt>中野</dt>
    <dd><em style="font-weight:bold; font-size:14px;">倒産した会社の社長は辛い</em>ですね。更生計画は大幅に短縮して終結し普通の会社に戻ったのですが、その後が大変だったんです。富士機工電子は電子部品をつくっている会社で、お客様はパソコンメーカーやプリンターメーカーが多かった。そこにITバブルの崩壊が直撃し、パソコン関連市場に急ブレーキがかかりました。お客様からのオーダーが3割、4割となくなっていきましたから大赤字です。いつ回復するのか目処が立たないまま半年、1年が過ぎ、回復するまでに1年半から2年かかりました。あのときはリストラも行い、本当にしんどかったです。</dd>
    <dt></dt>
    <dd>ITバブルの崩壊は経営を直撃しただけではなく、ファンドの投資という点でも厳しい場面を迎えました。最終的にはある大企業に富士機工電子を買収していただいて、私の任務は終了しました。ただし、買収された後も1年間私が社長をやるという条件で。先方にすぐ社長に出せる人がいないための措置でした。<em style="font-weight:bold; font-size:14px;">この成功とも失敗とも言えない社長経験は、その後の自分に大きく影響しています。</em>「経営は怖い！」と。</dd><br>

    <dt>丸山</dt>
    <dd>経営にはいろいろな怖さがあると思いますが、どのような怖さでしょうか。</dd>
    <dt>中野</dt>
    <dd><em style="font-weight:bold; font-size:14px;">事業を選ぶことの怖さ</em>です。崩壊した市場でうまくやれる人は誰もおらず、そうなったらひたすらリストラして景況が回復するのを待つしかありません。そして、私は投資家として崩壊の起こり得る波の激しい市場を選んでしまった。このことについてはいろいろ考えました。事業によって安定している市場もあれば波の激しい市場もあり、時代状況の変化もそこに大きな影響を与えます。それをよく見極めて事業を選ばないと大変なことになる。この経験からは本当に多くのことを学びました。</dd><br>
    
    <dt>丸山</dt>
    <dd>そしてファンドに戻り、産業再生機構入りされましたね。</dd>
    <dt>中野</dt>
    <dd>成功とも失敗とも言えない経験をしてファンドに戻ったわけです。自分のなかで整理のついていないものを抱えながら、次はどうすればよいか。そんなタイミングで産業再生機構の話をいただきました。私は自分が経営者として企業再生に取り組んできましたから、その経験を活かして「今度は失敗しないぞ」と思いました。</dd><br>

    <dt>丸山</dt>
    <dd>もう一回勝負しないとスッキリしない、と。</dd>
    <dt>中野</dt>
    <dd>「そうです。<em style="font-weight:bold; font-size:14px;">これで終わらせるわけにはいかない。</em>当時は「国を再生するには銀行と企業を再生しなければいけない」と小泉政権が法律をつくり、金融庁を動かし、産業再生機構を設立したところでした。私は倒産した会社の再建をやっていたのである意味、時代の先取りをしていたので、この知見を活かそうと思いました。私が富士機工電子で株主として、社長として企業再生に取り組んだのは5年間。特定の1社に深く関わったわけですが、企業再生にはいろいろなパターンとやり方があります。<em style="font-weight:bold; font-size:14px;">1社やっただけでは企業再生がどういうものかはわからないので、他にもどんな企業再生があるのか勉強したい</em>という思いもありました。</dd><br>
      
      <dt>丸山</dt>
      <dd>どんな企業の再建に関わりましたか。</dd>
    <dt>中野</dt>
    <dd>金門製作所、大京、カネボウ、三景、スカイネットアジア航空です。<em style="font-weight:bold; font-size:14px;">どれも企業再生ですが、病気が違えば治療の仕方も異なるように、再建のやり方も異なります。</em>産業再生機構ではいろいろな企業再生のパターンを見ることができました。<br>
