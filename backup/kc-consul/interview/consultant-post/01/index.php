             <h3 class="interview_detail_title">シティック・キャピタル・パートナーズ・<br>ジャパン・リミテッド</h3>
<h4 class="interview_detail_subtitle">日本代表　中野 宏信氏</h4>

             
             <div class="content-lead">エンジニアからMBA留学を経てキャリアチェンジし、日本における黎明期の投資ファンドや更生会社の経営者、産業再生機構などを経て現在、中国系ファンドの日本代表を務める中野宏信氏。その時々の時代における先端的な仕事ばかりできる理由は、果たしてどこにあるのだろうか。</div>
             
            	<div class="clear green b content_title_view">MBA留学を契機としてコンサルタントへキャリアチェンジ</div>
            		<dl>
    <dt>丸山</dt>
    <dd>中野さんと初めてお会いしたのは1989年、米国のペンシルバニアでした。当時、リクルートで採用業務を担当していた私はMBA留学中の中野さんを採用すべくコンタクトを取ったのですが、留学後、中野さんは戦略コンサルのCDI（コーポレイトディレクション）に入社されました。多くの選択肢の中からCDIを選択した理由は何でしょうか。</dd>
    <dt>中野</dt>
    <dd>それを説明するには大学時代まで遡らないといけません（笑）。私は工学部金属工学科の出身で、卒業生の大半は鉄鋼会社や自動車メーカーに就職していました。結果として私もトヨタ自動車に就職したのですが、<em style="font-weight:bold; font-size:14px;">エンジニアを一生の仕事にしようとは思っていませんでした。</em>トヨタを選んだのは海外駐在やマーケティングなど、エンジニア以外のオポチュニティがいろいろあったからです。しかし実際にはエンジンやサスペンションなどを鋳造する技術開発の部署に配属されました。</dd>
    <dt></dt>
    <dd>当初は周囲に馴染んで働いていましたが、次第にムクムクと<em style="font-weight:bold; font-size:14px;">「これは自分が一生やる仕事かな？」</em>という思いが湧いてきました。私はエンジニアリングよりもビジネス、経営に関心があったからです。また、将来を考えるとトヨタという大きな組織のなかで、うまくいけば部長、取締役になって、その後は関連会社の役員や社長になって終わり、という姿がリアルに想像できました。それが幸せという人もいるでしょうが、私はそれで一生を終わりたくないと思った。<em style="font-weight:bold; font-size:14px;">それで5年務めてトヨタを辞め、自費でMBA留学</em>しました。</dd>
    <dt></dt>
    <dd>留学の目的は、MBAを契機としてビジネスや経営の方向にキャリアを進めていくことでした。明確にやりたいことがあったわけではありませんが、エンジニアのバックグラウンドでも<em style="font-weight:bold; font-size:14px;">MBAを取得すれば他の道へ行けるチャンスが広がるし、英語の習得や人脈の構築もできる。</em>今でもあの二年間は正しい選択だったと思います。ただ、留学した大学は卒業後、金融方面に行く人が多いところでした。私はビジネスや経営をしたかったので、それらをしっかり勉強できるコンサルティング会社を選びました。<em style="font-weight:bold; font-size:14px;">CDIに入ったのは、メンバーの人たちとケミストリーが一番合ったからです。</em>当時はとても勢いがあり、一番熱心に誘ってくれた。他社と迷うということはありませんでしたね。</dd><br>

    <dt>丸山</dt>
    <dd>実際にCDIに入社されてみて、経営の勉強になりましたか。</dd>
    <dt>中野</dt>
    <dd>非常になりました。コンサルタントが何を売っているかというと、一つは「提案」です。いただいたテーマについて調査、分析し、その事実や結果に基づいて企業がどう問題をとらえ、どうすべきかのアイデアを売るわけです。しかもそのアイデアはきわめてロジカルでなければいけない。そういう仕事なのでコンサルタントに求められる能力はロジカルに物事を考える力、きちんと事実を調査、分析する力、そしてプレゼンテーションの力です。ところが正直、それまで私は物事を本質的に考えるトレーニングを積んでいませんでした。<em style="font-weight:bold; font-size:14px;">物事を深く突き詰めて考え、ロジカルに組み立てて自分なりの結論を出し、うまく相手に伝えていく。</em>コンサルタントになって初めて、そういう訓練をしっかりできました。この経験は大きかったと思います。</dd>
    <dt></dt>
    <dd>また、いろいろな業種の経営に関する問題を与えられ、それを解くことによって、机上ではあるけれど経営についてよく理解できるようになりました。そして何より、いい仲間が多かった。後に産業再生機構に入ったのもCDIで知り合い、当時産業再生機構COOを務められていた冨山和彦さん（現経営共創基盤代表取締役CEO）とのご縁があったからです。ここで会った人とのご縁がいま、私のパワーになっています。</dd><br>
    
