             <p>シティック・キャピタル・パートナーズ・ジャパン・リミテッド</p>
<p>日本代表　中野 宏信氏</p><br><br>

            	<div class="clear green b content_title_view">オーナー企業での教訓を活かし投資ファンドへ</div>
            		<dl>
    <dt>丸山</dt>
    <dd>CDIの後は、あるベンチャー企業へ転職されました。どのような経緯でしたか。</dd>
    <dt>中野</dt>
    <dd>この転職は知人の友人のヘッドハンターの紹介です。コーポレイトディレクションに5年いたので、<em style="font-weight:bold; font-size:14px;">そろそろリアルなビジネスに戻りたい。</em>経営を机上で勉強できたのでそれを活かして自分で経営をしたいと考えていた時期に、ちょうど声をかけてもらいました。そこでは本業とは別の事業を新しく子会社を創って始めるところで、ベンチャーであることと、トップの補佐役というマネジメントに近い位置で仕事ができる点に魅力を感じました。</dd>

    <dt> </dt>
    <dd>ここでの経験は、<em style="font-weight:bold; font-size:14px;">CDIとは別の意味で勉強になりました。</em>「超」の付くオーナー企業でかつ若い会社でしたから、社内はいろいろな人の寄せ集めのようなところがあって、その難しい面を沢山見ました。オーナー企業では当然、トップの影響力が非常に強いため、そのビジョンや方針にしっかりした芯が通っていないとブレてしまう。加えて、寄せ集め的な組織ではムダが多く、軋轢が生じやすい。</dd><br>

    <dt>丸山</dt>
    <dd>このベンチャー企業は一年でお辞めになっていますが、やはりそういったことが影響したのですか。</dd>
    <dt>中野</dt>
    <dd>CDIからベンチャーに移ったときは正直、あまりよく考えていなかったんです。学んだことはありますが、キャリアという視点で考えるとこの転職は、選択として間違っていたと思います。なので次にどうするかはじっくり考えた上で決めないといけない。辞めた後は半年以上、ぶらぶらと充電していました。人に会ったりいろいろな情報をインプットしながら、自分の頭のなかを整理しました。そんな時期に<em style="font-weight:bold; font-size:14px;">MBA留学で同級生だったヘッドハンターから声がかかりました。</em>彼のクライアントはアドバンテッジパートナーズというファンドで、代表パートナーのリチャード・フォルソムもMBAの同級生でした。<br>
「リチャードのところが人を探している。今度新しいことをやる。それはファンドというものだ」
</dd>
    <dt> </dt>
    <dd><em style="font-weight:bold; font-size:14px;">当時、ファンドはまだ日本になかった</em>ので、ヘッドハンターの言い方はそんな風になりました。私も不勉強でよくわからなかったのですが、実際に会って話を聞いてみると「面白い」と思い、入社することになりました。</dd><br>

    <dt>丸山</dt>
    <dd>何が面白いと思ったのですか。</dd>
    <dt>中野</dt>
    <dd>株主として経営に直接関与できることです。オーナー企業の意思決定がオーナーの一存で決まるのは株主だからです。オーナーが優れた見識とリーダーシップの持ち主であれば素晴らしい会社になるでしょう。しかし、多くのオーナー企業の実態はそうではなく、往々にしてビジョンや戦略がふらつき、そのたびに下の人間は苦労します。だったら自分が株主になってしまえばいいわけです。また、コンサルタントは経営戦略をつくる訓練をしていてアイデアはいっぱいありますが、自分で実行することはできない。ところが<em style="font-weight:bold; font-size:14px;">ファンドという立場で株主になれば、自分たちの考えたアイデアを投資先企業に実行してもらうことができる。</em>実際にはとても難しいことなんですが、コンサルタントの立場で戦略やアイデアをつくり、それを株主として実行できる。投資がうまくいけばリターンも得られる。そうした点が面白いと思いました。</dd><br>
    
