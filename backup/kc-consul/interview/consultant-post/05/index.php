             <h3 class="interview_detail_title">株式会社グロービス・キャピタル・パートナーズ</h3>
<h4 class="interview_detail_subtitle">パートナー Chief Operating Officer　今野 穣氏</h4>

             <div class="content-lead">日本最大の独立系、ハンズオン型ベンチャーキャピタルであるグロービス・キャピタル・パートナーズは1996年に第1号ファンドを組成して以来、100社を超える起業家へ約400億円の資金を継続して投資し日本の産業創造に貢献してきた。コンサルティングファームから転職し、同社のリーダーとして活躍するパートナーの今野穣氏はなぜ、ベンチャーキャピタリストへの転身を決断したのだろうか。</div>
             
             
            	<div class="clear green b content_title_view">転職の決め手は「そこで働いている人たちの元気さ」</div>
            		<dl>
    <dt>入江</dt>
    <dd>新卒でコンサルティングファームに就職したきっかけは何ですか。</dd>
    <dt>今野</dt>
    <dd>大学生のとき、私は司法試験を受験するためにわざと1年留年しましたが、試験の結果は残念ながらダメで、それがわかった夏から就職活動を始めました。すでにその時期には主だった企業の採用試験は終わり、残っているのはインベストメントバンクとコンサルティングファームぐらいしかなかったんです。そうした企業をいくつか受けて内定をもらった中から、当時のアーサーアンダーセンビジネスコンサルティングに入社しました。決め手は新卒採用第一号で前例がなかったこと、人事の方に手厚い対応をしていただいたことです。司法試験でムダにした1年という時間を挽回したい。それには上が詰まっている組織ではなく、一気に成長できるところがいいと考えました。</dd><br>

    <dt>入江</dt>
    <dd>当時はどの部門でどんな案件を手掛けていたのですか。</dd>
    <dt>今野</dt>
    <dd>部門は戦略チームで案件としては製造業が多く、グローバルサプライチェーンの供給プロセス改革プロジェクトや、組織再編に伴う業績評価体系構築などを担当しました。無駄にした時間を取り戻そうと仕事に集中して3年でマネージャーになり、その後はある大手メーカーの中期計画策定プロジェクトなどを担当しました。新卒だった私を採用するコストと引っ張ってもらったご恩はお返しできたかなと思ったところで退職し、2006年7月にグロービス・キャピタル・パートナーズに移りました。</dd><br>

    <dt>入江</dt>
    <dd>順調に昇進していた今野さんが転職を考えるきっかけは何でしたか。</dd>
    <dt>今野</dt>
    <dd>正直に言うと、飽きてしまったからです。無力感もありました。基本的にコンサルティングは受託ビジネスなので先にテーマがあってそこに最善を尽くすという形になっていますが、そう簡単に会社は変わりません。ベンチャーキャピタルに来るとよくわかりますが「組織は人なり」なので、おかしくなっている会社の中の人が変わらない限り、どんなソリューションを提案しても変わりにくいんです。そんなもどかしさや、「自分は役に立てているのだろうか」という思いがありました。では次に何をするかですが、リスクを軸に考えると大手向けの受託ビジネスと起業が両極に位置します。その中間くらいの仕事がいいなと考え、人の紹介で当社ともう一社だけ話を聞きに行き、最終的にグロービス・キャピタル・パートナーズに決めました。</dd><br>
   
   
     <dt>入江</dt>
    <dd>グロービス・キャピタル・パートナーズに転職した決め手は何だったんですか。</dd>
    <dt>今野</dt>
    <dd>ベンチャーキャピタルという仕事と、そのエコシステムで働いている起業家たちです。「なんて元気で前向きな人たちなんだ！」と。何か世の中に新しいものを生み出そうとしている、モチベーションが極めて高いコミュニティのなかで仕事ができるのは面白そうだと思いました。それに、コンサルタントは時としてクライアントと利害や目指すべき方向が合致しないときがありますが、ベンチャーキャピタルと投資先企業は判断する時間軸の違いで意見が分かれることはあっても、出資者かつ当事者としてのコミットメントがあり、「新産業の創造」、「企業の成長」という意味での大きな方向性では齟齬がない。そんな匂いも入社前に感じました。
</dd><br>


