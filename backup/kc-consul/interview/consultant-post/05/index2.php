    <dt>今野</dt>
    <dd>ベンチャーキャピタルは総合格闘技のようなところがあって、案件開拓ではネットワーキングやコミュニケーション能力が大切で、審査のときは分析力、投資した後の事業を大きくするところはコンサルティング力、売却のときは投資銀行のような能力が必要です。つまり営業、アナリスト、コンサル、投資銀行の組合せなので、すべてが一からのスタートだと厳しい。ところがコンサルタント出身者は最初のよりどころを一つ持っているわけです。また、コンサルタントは経営に関するフレームワークで物事を整理するのに長けているので、走りながら考える起業家とは相性がよいです。ただし、MBA保持者や前職がコンサルタントだからといってベンチャーキャピタリストとしてうまくいくとは限らず、過去においてはMBA取得の有無による相関関係はないですね。</dd><br>

    <dt>入江</dt>
    <dd>それはなぜですか。</dd>
    <dt>今野</dt>
    <dd>MBAが悪いということではなく、MBAとは別のところにパフォーマンスを上げる要因があるのだと思います。フレームワークをつかって仮説をつくるとき、情報が少ないと地に足のついていないサジェスチョンしかできません。つまり、フレームワークよりベースとなる情報に対する好奇心と、それを取りに行くフットワークのほうが大切なんです。また、ベンチャーキャピタリストは人好きである必要があります。投資は事業の目利きが大事とよくいわれますが、それ以前に投資すべき人や会社と出会い、どう信頼関係を結び、エクスクルーシブな関係にもっていけるかが極めて重要です。たとえば2年前に私がモデレーターを務めたカンファレンスで、あるネット企業でそれまでの赤字を一掃する素晴らしいサービスをつくった人と知り合い友人になりました。そして最近、「会社から役員昇格の話をもらったが独立して自分のビジネスをやりたい」という相談を退社前に受け、「創業直後の会社に出資はできないけれど、ある程度のところまできたら一緒にやろう」という話になりました。こういう関係性ができていると他社がまったく知らない案件に投資することができ、それがリターンに直結します。ピカピカの案件を持つには時間をかけ、仕事の利害関係に入る前の人間関係をつくれるかどうかにかかっているんです。
</dd><br>

   
     <dt>入江</dt>
    <dd>投資以前の人間関係構築が重要なのですね。</dd>
    <dt>今野</dt>
