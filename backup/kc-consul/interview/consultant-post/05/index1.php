             <p class="interview_date">掲載日: 2013年12月17日</p>
             <p>株式会社グロービス・キャピタル・パートナーズ</p>
<p>パートナー Chief Operating Officer　今野 穣氏</p><br><br>

            	<div class="clear green b content_title_view">米国のベンチャーキャピタルと同じ土俵で勝負する</div>
            		<dl>
    <dt>入江</dt>
    <dd>実際に入社してみて、入社前に感じたこととズレはありませんでしたか。</dd>
    <dt>今野</dt>
    <dd>いま申し上げた点については入社前に感じた通りでしたが、ビジネスとして儲けを出すところは想定していたよりもタフでした。当社に入社する人たちは前職できらびやかな肩書きや成功体験を持っているのですが、たいてい最初は撃沈されます。コンサルティング会社であれば顧客企業の売上や経営基盤がある程度担保されているので、業務改革やコスト圧縮で利益を生み出せます。しかし、ベンチャーはまず売上からつくらなければいけません。売上をつくるのはお客様にどう製品やサービスを受け入れてもらうかという話であって、コンサルタントとして組織をうまく動かし高いパフォーマンスを発揮していた人でも売上を0から1にすることは難しい。ではどうするかというと0を1にできる人を連れてくればよいのですが、最初は「自分でできる」と思っているので己の力の無さを痛感します。私も最初の半年間はとてもつらかったです。それまで培ってきた能力が、ベンチャーキャピタリストをやる上ではこんなにも役に立たないのかと。</dd><br>

    <dt>入江</dt>
    <dd>つらい半年間を乗り越えられたのはなぜでしょう。</dd>
    <dt>今野</dt>
    <dd>要因は3つあります。1つ目は根っこの気持ちが折れなかったこと。日本に新しい産業を生み出したいという転職する時の思いがぶれなかったので、表面的なつらさはありつつも楽しめました。2つ目は慣れで、半年ほどでだいたい業務を理解できました。3つ目は自分がメインの投資案件を早く持てたことです。コンサルタントと一緒で新人は最初、サブの担当者として案件に入るのですが、私は半年で自分がメインの案件を持たせてもらうことができ、ラーニングが早まりました。ベンチャーキャピタルは赤字会社に出資するほうが多いので、たとえば投資委員会から決済を得て3億円を出資すると、売上を増やさない限りそのお金が1000万円、2000万円とみるみるうちに減っていきます。「やばい、このままいくと2年で何もなくなる……」というギリギリの感覚を味わいながら何をすればよいのかを考え行動するのと、サブの立場から「こうすればいいじゃないですか」というのでは全然違いますから、それはとても大きな変化でした。</dd><br>
    
    
    <dt>入江</dt>
    <dd>ベンチャーキャピタリストという仕事の楽しさはどこにあると感じていますか。</dd>
    <dt>今野</dt>
    <dd>細かい喜びはいっぱいあって、通勤電車で自分が投資している会社のサービスを使っている人を見るととても嬉しいですし、投資先の会社の人が成長していく姿を目の当たりにするのも嬉しいです。数年で急成長している会社の中の人は無茶苦茶成長します。大企業に勤めていたときは従属的な立場でしか話をできなかった人が社長になり、「会社の看板を背負うとこんなことまで言えるようになるんだ」と驚かされるのは本当に嬉しい瞬間です。あと、投資先の社員数が増えると雇用創出という意味でやりがいを感じます。実は、リターンが出たときはあまり楽しいとは感じません。投資した会社が上場を間近に迫った段階で役員を退任し、実際に上場していくときには、「この会社を世に送り出して本当に大丈夫かな」と、むしろ緊張感でいっぱいになります。
</dd><br>


	<dt>入江</dt>
    <dd>ベンチャーキャピタルのパートナーとしてのやりがいは何でしょうか。</dd>
    <dt>今野</dt>
    <dd>パートナーとそうでない人の大きな違いは、投資家対応の有無にあります。幸い我々は圧倒的な実績を出しているので、海外の機関投資家から多くのお金を預かっています。昨日も香港で海外の機関投資家とコミュニケーションをとっていたのですが、世界の名だたる投資家たちと接していると自分が日本を代表しているような使命感を持ちます。国内だけでやっていたら限られたパイの奪い合いにしかなりませんし、海外から集めたお金でLINEのような新しいサービスを生み出せたらいいレバレッジになりますよね。新しい産業を生み出せる触媒な装置はベンチャーキャピタルしかないと私は思っていて、そこにこの仕事のやりがいや使命を感じています。
</dd><br>


	<dt>入江</dt>
    <dd>他のベンチャーキャピタルと御社の違いはどこにあるのでしょうか。</dd>
    <dt>今野</dt>
    <dd>比較的大きな金額を投資するという点で、他社より多くリスクを取りにいきます。そして投資先には必ず非常勤役員として経営チームに入るので、投資先との距離感や責任感がまったく異なります。単に数千万円を出資し、財務諸表をもらって眺めるだけでは何の学びもありませんが、我々は事業を大きくする過程で起きるさまざまな課題を役員として把握し、どんな打ち手を取るべきかディスカッションしていくので、その案件が成功するか否かは別にして学びが蓄積されていきます。「このチームのケミストリーでは将来、こんな事態が生じそうだからこの点に注意しておこう」といった洞察力が身に付いてくるので、次の案件に対する精度がまったく違ってくるのです。また、すでにお話したように海外の機関投資家が多くリターンへの要求度が高いので、そこから始まるさまざまなアクティビティは気合いが違います。海外の機関投資家はアメリカや中国、インドなど世界中のベンチャーキャピタルと比較してリターンの高いところに投資しますから、我々に投資してもらうにはアメリカのベンチャーキャピタル以上のリターンを出さなければいけない。そういうプレイエリアのなかで我々は仕事をしています。
</dd><br>
    
    
    
    </dl>
