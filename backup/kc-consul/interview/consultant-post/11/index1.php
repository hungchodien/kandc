<br />

<dt>羽生</dt>
<dd>薬剤師の人材紹介事業に関わって1年ほど経った頃、医師や薬剤師へのキャリアサービスを提供するジョイントベンチャーとして、このエムスリーキャリアを立ち上げることになったんです。会社設立に関われる機会はそう得られるものありませんし、新しい事業を自分の手で創れるチャンスだと捉えて、事業責任者としてそのまま転籍することに。そこでの経験が大きかったですね。ほぼ現場に裁量を委ねられることになり、自分で意思決定しなければならないことが増えた。上から干渉されない代わりに、失敗もすべて自分に跳ね返ってくる。そうした環境が自分には合っていたんでしょう。自分で考えたこと、感じたものから意思決定を重ねていくことでそうした環境にも慣れ、1年目に薬剤師の人材紹介で実績を上げ、2年目からは医師の人材紹介事業やメディア事業を担当。今まで薬剤師の人材紹介事業の営業管理を中心に見ていたところからより広い視点で事業を見渡せる立場に就いて、人材紹介とメディアの事業をどのようなバランスで進捗させていくか、そこでどう人を動かしていくかなど本当の意味でのマネジメントを経験することができ、そこで一気に視野が広がりました。また、自分の理解が及ばないところは、そこに精通したメンバーの力を借りながらビジネスを進めていくという姿勢も身につきましたね。</dd>
    
   
    
