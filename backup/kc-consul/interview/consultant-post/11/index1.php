    <dt>入江</dt>
    <dd>コンサルタントで得たスキルで活かすことができたというのは、具体的にはどんなことですか。</dd>
    <dt>羽生</dt>
    <dd>状況を整理して、他の人にわかりやすくアウトプットするということでしょうか。何事に対しても目的・ゴールを明確にし、チームのコミュニケーションをスムースにするスキルは事業会社でも通用しましたし、社会人になって早目に身につけておいて良かったと思いますね。それで社内でも多少は認められ、入社した年の秋に中期経営計画を策定するメンバーにもなり、今後のエス・エム・エスについて考える場に加わりました。当時、事業を創り出せる人材をいかに増やしていくかが会社としての大きな課題で、新たに立ち上げた薬剤師の人材紹介事業を若手に任せたいという経営陣の意図があり、そこに私は自ら手を挙げて、事業の責任者をさせていただくことになりました。</dd><br />
    
     <dt>入江</dt>
    <dd>そこで成果を上げられたわけですか？</dd>
    <dt>羽生</dt>
    <dd>いえ、勇んでそのポジションを引き受けたものの、何しろ自分でビジネスをリードしていくのは初めての経験でしたから、当初は苦労しました。事業で注目すべき数字やそもそもの事業構造もよくわからないまま組織を率いていて、いわばダッシュボードがない車を運転しているような状況。特に自分の至らなさを思い知らされたのが、人・組織のマネジメントでした。ほんの一例ですが、コンサルファームにいた時は、メンバーへの情報伝達などはFYIと書いてメールを転送するだけ。そこで齟齬が生じても、情報をきちんとキャッチアップしない側が悪いくらいの感覚。しかし、ここではそれは通用しない。いろんなレベル感のメンバーがいるなかで、どうマネジメントすればメンバーがやる気と成長実感を保ちつつ結果を出し続けられるのか、それがまったく理解できていなかったんですね。人の入れ替わりも起こったりして、業績も期待されていたほどには伸ばせませんでした。ただ、そこでもコンサルタント時代のキャリアが多少強みになっていて、物事を分析して課題を抽出するスキルだったり、あるいはグローバルなプロジェクトで異なる価値観の人間と協業した経験などは、事業を運営していく上でも役に立っていて、苦しいながらも逃げずに目の前の課題解決を重ねていくことで次第に状況が見えてきて結果が出るようになっていきました。</dd><br />

