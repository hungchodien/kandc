    <dd>けっして社長を目指していたわけではありません。エムスリーキャリアに参画した当初は、自分の将来のキャリアを考える余裕などありませんでしたし、とにかく事業を伸ばしていくために目の前の課題に必死で対処していくうちに、組織もできて数字も上がってきて、自分がやるべきことが見えてきたという感じです。社長に就いたのも、グループ内で経営人材をもっと育成していこうという方針が掲げられ、チャンスをいただいたという経緯です。</dd><br />
  

    <dt>入江</dt>
    <dd>ご自身を振り返られて、羽生さんはコンサルタント経験をベースにどんなことを意識されてこられたから、いまの経営者のポジションに就くことができたとお考えですか。</dd>
    <dt>羽生</dt>
    <dd>ひとつは、状況に合わせて自分を変えていけるかということですね。例えば、私はコンサルタント時代から一対多でプレゼンテーションするのは得意でしたが、実は一対一でコミュニケーションするのは苦手で、自分の考えを相手にその都度伝えて共通認識を作っていくことがうまくできなかった。そのことに気づいて、メンバーへの関わり方を変えようと努めましたし、組織の作り方も変えました。また自分の能力の限界を察知して、他のメンバーの力を借りるというスタンスでビジネスを進めたことも奏功しました。いま自分が置かれている状況の中で、どう変わるべきかをいち早く認識して修正していくことのできる能力が、コンサルタントから事業会社へ転身する際、特に弊社のように成長スピードが早く変化も激しい会社には求められると思います。<br />
あとは、逃げずに向き合い続けることでしょう。事業運営では様々な利害関係を調節して、関係者のベクトルをあわせる必要がある。その時、自分も含め人は論理だけでは動かず感情にも大きく左右されるので、最初からスムースに行くことの方が少ない。でも、諦めずにアプローチを工夫しながら、人や組織を動かしていく必要があります。コンサルティングのプロジェクトとは違い、事業に終わりはなく結果がすべて。組織で起きることは全て自分に関係があるという気持ちで、事業に向き合い続けることが重要と思います。
