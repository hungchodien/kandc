<br />
<dt>羽生</dt>
<dd>3年から5年ぐらいかけて、ひとつの事業の責任者になっていただければと考えています。コンサルタント経験者は論理的思考力や知的体力などを高いレベルで備えている方が多いので、短期間に一つでも多くのPDCAを回して事業について見えている範囲を広げていただきたい。3年から5年ぐらいというのは、私自身もそうでしたが、コンサルタントを数年経験して事業会社に移ってすぐに事業の責任者として通用するほど甘くはないからです。<br />
コンサルタントの中には、設定されたゴールに向けての論点整理や課題抽出は得意でも、自らゴールそのものを設定するという経験がない方が多いのではないでしょうか。ですから、まずはここでいろんな失敗をして気づきを得て、マインドを変えて、自らPDCAを回して事業をリードしていくためのゴールを設定し、それをマネジメントできるようになってほしい。</dd>
<br />
<dt>入江</dt>
<dd>長い目で見て経営人材を養成されようとされているのですね。では、最後に若手のコンサルタントの方々に、これからのキャリアを考える上でのアドバイスをお願いします。</dd>
<br />
<dt>羽生</dt>
