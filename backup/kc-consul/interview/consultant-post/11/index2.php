<dt>羽生</dt>
<dd>加えて、いま私たちが取り組んでいるのは「超高齢社会に適した医療提供体制づくりの支援」であり、「医療人材の最適な配置と能力の最大化」というこれからの世の中にとって重要なテーマです。それをビジネスを通して解決していくことができる。しかも、日本だけではなくグローバルにもビジネスを展開しようとしており、それは当社に参画する大きな醍醐味だと思います。私自身も、今後10年20年と自分のキャリアを捧げてもいい、それぐらい価値のあるテーマだと感じています。</dd><br />

<dt>入江</dt>
