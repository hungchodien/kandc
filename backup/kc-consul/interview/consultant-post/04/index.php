    <dd>はい。私は新卒で当時の国際電信電話、現在のKDDIに入社し最初は法人営業を担当しました。その後、事業開発本部へ異動し、主に新規事業の企画や既に立ち上がっているグループ会社のマネジメントに関わる機会が増えました。もともと私は企業経営や事業開発に対する関心があったのですが、そこからさらに興味が強くなっていきました。折しも90年代後半で、空前のベンチャーブームが盛り上がっている頃。KDDIは素晴らしい会社ですが、私自身のキャリアを切り拓くという意味で一念発起する必要があるのではと思い、2001年にドリームインキュベータ（以下、DI）に転職し2006年末まで働いた後、2007年にジェイアイエヌに転職し、現在に至ります。</dd>
