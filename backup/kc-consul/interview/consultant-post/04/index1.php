            	<div class="clear green b content_title_view">事業会社へ転身した決め手は「優れた経営者との出会い」</div>
            		<dl>
    <dt>入江</dt>
    <dd>
      <p>DIからジェイアイエヌへ転職されたきっかけは何ですか。</p>
    </dd>
    <dt>香川</dt>
    <dd>DIでの経験を積むにつれ、事業開発を事業会社の立場で主導的に進めていきたいという気持ちが強くなっていきました。そんな頃にベンチャー経営者に集まっていただき、想定される経営課題に対し「自分ならどうするか」を考えるという勉強会を主催する機会に恵まれ、そこで弊社社長の田中と接点を持つようになりました。最初にお会いしたときはまだ上場前だったのですが、組織の成長はリーダーによって決まるという説が正しいとすると、田中は卓越した経営者であるとの印象を私は強く持ち自ら志願する形でジェイアイエヌに入社することになりました。当初は経営企画室もなかったですから、管理本部のシニアマネージャーとして入社し、様々な経営課題についてトップマネジメントの指示を受けて組織横断的に解決していくという形で、人事制度の整備、EC事業の立ち上げ、店舗オペレーションの改善、社員教育など、様々な経験を積み、現在に至ります。</dd>
    <br>

    <dt>入江</dt>
    <dd>
      <p>田中社長が経営者として優れているとお感じになったのはどんな点ですか。</p>
    </dd>
    <dt>香川</dt>
    <dd>
      <p>日頃から学び続けていることです。ベンチャー企業の経営者は上場した途端に気が緩んでしまう経営者が少なくないのですが、田中は企業の成長継続のために謙虚に学び続けるという姿勢が今でも変わりません。20年以上社長業を続けるなかで、学び続けないといろいろな問題を乗り越えられないと身をもって経験しているからこそ、その姿勢を堅持しているのだと思います。</p>
    </dd>
    <br>

    <dt>入江</dt>
    <dd>香川さんがジェイアイエヌに入社した時期は現在のような躍進が始まる前で、経営的に厳しい時期もあったと聞きます。どのような状況だったのでしょうか。</dd>
    <dt>香川</dt>
    <dd>
      <p>一般的にいえることですが、上場を実現したベンチャーでも事業環境の変化に合わせてビジネスモデルをブラッシュアップしないと強みが相対的になり、その先の継続的な成長が難しくなります。ジェイアイエヌは2006年にヘラクレス（現JASDAQ）上場を果たしたのですが、そのような落とし穴に結果的にはまってしまいました。当時はファッション性の高いアイウエア事業と服飾雑貨事業のSPA化を進め、両者を融合した事業をつくるという成長戦略を立てていたのですが、結果的に時代のニーズとマッチしていませんでした。しかも稼ぎ頭であったアイウエア事業の収益性が大手企業の攻勢が強まったことで揺らいできました。入社前、そして入社直後はもっと順調に成長が続くものと思っていたのですが、甘すぎました。</p>
    </dd>
    <br>
    
    <dt>入江</dt>
    <dd>
      <p>その時、この転職は間違いだったかもしれないとは思いませんでしたか。 </p>
    </dd>
    <dt>香川</dt>
    <dd>
      <p>ビジネス的に正しいことをやっているとの確信があったので、そうは思いませんでした。服飾の世界ではユニクロやGAPなどSPAで大成功を収めた企業が存在し、有力な経営スタイルの一つとなっています。アイウエア分野でも当然SPAは成り立つし、そこでジェイアイエヌは有力なプレーヤーになり得るとの確信が私にはありました。つまり、現状は手段が間違っているだけだから、それを調整さえすればいいと。ただ、結果的にみるとその調整は決して簡単ではなく、レンズ追加料金無料（*単焦点のみ）のオールインワンプライスを導入する一方、「エア・フレーム」という有力な新商材を開発し、それを売るためにプロモーションに注力する、というように、創業以来の大きなチャレンジをいくつも重ね、それらが積み上がっていったからこそ現在のジェイアイエヌがあるのだと思います。 </p>
    </dd>
    <br>

    <dt>入江</dt>
    <dd>
      <p>そんな会社の変革時に香川さんはどんな役割を担当されましたか。 </p>
    </dd>
    <dt>香川</dt>
    <dd>当時の主な役割としてはお店の立て直しでした。業績の落ち込みに伴い辞めるスタッフが続出したり、お店ごとにオペレーションがバラバラだったりといった問題があったので、店舗オペレーションを標準化し定着させ、新しく打ち出す施策をきちんと展開できる体制を整えました。これは今後100店、200店とお店が増えても同じルールで運営できるようにするためでもあります。つまり大量出店が前提の成長戦略を考えていたわけですが、そこでもう一つ重要になるのが人材の確保と教育です。大量出店には人材教育の体系化が必要で、私自身も眼鏡づくりの勉強をしながら教育体系を構築していきました。</dd>
    </dl>
