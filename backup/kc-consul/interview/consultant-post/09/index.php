    <dd>実は、日本で戦略グループが立ち上がったのは私が入社した92年で、戦略コンサルタントの募集はなかったんですね。そもそも新卒を採用している戦略ファーム自体がほとんどなくて、そのうちの一社の外資系の老舗ファームのセミナーにとりあえず参加してみたのですが、これが強烈につまらなくて……そのファームが70年代に開発した経営コンセプトなどを滔々と語られて、まったく興味が持てなかった。それで他を当たろうとアンダーセンコンサルティングを訪れてみたところ、「これからは情報技術が戦略を規定する」などと斬新なビジョンを掲げていて、ちょっと惹かれたんですね。その後、選考に進んで最初に内定をもらえましたし、また、近々新たに戦略グループが発足して、入社後に社内でトランスファーできる機会もあるということなので、じゃあ、ここでいいかと決めました。結構いい加減な選択でしたね。</dd><br>
    
