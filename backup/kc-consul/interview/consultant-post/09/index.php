             <div class="content-lead">アクセンチュアでの戦略コンサルタントを経て、企業再生支援機構でマネージング・ディレクターとして実績を上げ、現在JVCケンウッドの取締役を務めている田村誠一氏。経営者を志向するコンサルタントの方々にとっては、たいへん参考になるキャリアに違いない。インタビューを通して、田村氏はこれまでのキャリアを紐解いていく。</div>
             
