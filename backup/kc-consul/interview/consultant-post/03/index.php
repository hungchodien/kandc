    <dd>渡部さんは住友電工時代にアメリカでMBA留学を経験し、帰国後しばらくしてベイン・アンド・カンパニーに転職されています。事業会社からコンサルティングファームに移られた経緯はどのようなものでしたか。</dd>
    <dt>渡部</dt>
    <dd>エンジニアだった私は、留学後はある事業部の企画担当になり、事業計画の立案などに加えて、海外工場進出や提携などさまざまな国際交渉にも携わりました。そのなかで、ある韓国企業に技術供与をする仕事を担当し、韓国へ出張する機会がありました。交渉相手はその韓国企業の社長と、技術パートナーであるイギリス企業のシニア・バイス・プレジデント。交渉相手の二人はディシジョンメーカーであるのに対し私は決定権のない立場でしたので、二人からその場で決断を求められても、立場上即決できませんでした。後日稟議書を書いて社内に回し、承認印をいくつももらった後でないと返答できなかったわけです。<br>
    そこで上司に「なぜ即答できる人間を交渉に送り込まないのか。これでは対外的な信頼を得られない」という話をしたところ、上司からは「20年待て。その頃、あなたは部長になっているかもしれないし、稟議書もいらなくなっているかもしれない。」と言われました。その答えを聞いて「20年は待てないな……」と思ったのが転職を考えるきっかけになりました。国際交渉の場に平社員である私を送り込んでくれたことにはとても感謝していますが、意思決定に長い時間がかかることと、自分が経営に携われるまでに非常に長い時間がかかると気付いたときに「じゃあ、どうしようか？」と考え始めたのです。</dd>
