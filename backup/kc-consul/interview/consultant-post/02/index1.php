               <p>シティック・キャピタル・パートナーズ・ジャパン・リミテッド</p>
<p>日本代表　中野 宏信氏</p><br><br>

              
            	<div class="clear green b content_title_view">面白い仕事をできる秘訣は「人の縁」</div>
            		<dl>
    <dt>丸山</dt>
    <dd>逆にコンサルタントで身に付けた経験や能力で、事業会社のトップになったときに役立ったのはどんなことでしょうか。</dd>
    <dt>中野</dt>
    <dd><em style="font-weight:bold; font-size:14px;">コンサルタントと経営者は別物</em>です。私がコンサルタントとして関わった顧客は大企業で社員もそれなりにしっかりした会社ばかりでしたが、社長をやったのは売上が100億円くらいの規模の会社です。パスやトラップなど基礎技術のしっかりしているサッカーチームとそうでないチームでは、監督のやるべきことや苦労することは全然違います。経営もそれと同じで、コンサルタントをやっているときは基礎技術のある企業がお客様でしたから「こうやればいいんですよ」とアドバイスすればよかった。しかし社長をやった会社はパスもトラップもうまくできなかったし、そもそも試合に来ない社員もいました。そういう組織を預かって経営をするには、<em style="font-weight:bold; font-size:14px;">正しい戦略を描いたからといってその通り動いてもらえるわけではない</em>という苦労がありましたね。</dd>

    <dt> </dt>
    <dd>したがってコンサルタントの経験が経営に直接活かせる部分がたくさんあるとは言えませんが、もちろん役立つこともあります。とくに<em style="font-weight:bold; font-size:14px;">自分のメッセージをわかりやすく社員に伝えることに関しては、コンサルタントの経験が役に立ちました。</em>会社のなかにはさまざまな水準の、さまざまな立場の人たちがいます。いくらメッセージが正しかったとしても、相手に合わせてわかりやすく伝えないと意味がありません。</dd><br>

    <dt>丸山</dt>
    <dd>中野さんがシティックの日本代表というポジションに就くことができた要因は何だったと思いますか。</dd>
    <dt>中野</dt>
    <dd>結局、私の転職はどれも人に引っ張ってもらい決まっています。人の縁があったからアドバンテッジにも産業再生機構にも入れたし、シティックに引っ張ってもらうことができた。ですから、<em style="font-weight:bold; font-size:14px;">今のポジションにいる要因は「ご縁」</em>です。</dd>

    <dt>丸山</dt>
    <dd>日本初であったファンドの運営や企業再生、中国ビジネスなど、その時々の先端的な仕事をしてこられたのはなぜでしょうか。</dd>
    <dt>中野</dt>
    <dd>私のキャリアは最終目的をハッキリして歩いてきたわけではありません。その時々のチャンスとご縁でいろいろな選択をしてきたのですが、結果として見るとその時代の先端をいく分野の仕事ばかりやってきました。それは<em style="font-weight:bold; font-size:14px;">自分で時代を切り拓いてきたというより、時代を切り拓いている人とのご縁があったから</em>です。アドバンテッジではリチャード、産業再生機構には冨山さん。そういう時代の先端を走っている人に私は引っ張ってもらいました。</dd><br>

    <dt>丸山</dt>
    <dd>2007年に中野さんがシティックに移られたときと現在とでは、中国のイメージはだいぶ違います。当時としては海のものとも山のものともつかなかった中国のファンドに行く決断をできる決断力が、大きな秘訣のようにも思います。</dd>
    <dt>中野</dt>
    <dd>大した決断とは思っていません。<em style="font-weight:bold; font-size:14px;">要は新しいもの好きで怖いもの見たさがあって、「これから」というものにすごく興味が湧きます。</em>戦略的に考えるというより、先端的なことをやっている人たちの話を聞き、「なるほど！」と思ったことをやってきました。そして、<em style="font-weight:bold; font-size:14px;">私を引っ張ってくれた方々は皆、能力も人格も素晴らしい人たちです。そういう人たちとのご縁を大切にすることが、やはり一番の秘訣かもしれません。</em>声をかけられたからといって、誰にでも付いていっていいわけではないですよ（笑）。</dd>
    <dt> </dt>
    <dd>能力だけある人は世の中にいっぱいいますが、能力も人格も優れていて、かつビジョンを持っている人はあまりいません。普通はそうした人物とはなかなかご縁を持てませんが、私は幸運なことにご縁を持たせていただき、チャンスを与えてもらった。世の中には私程度の人間は他にもたくさんいますが、なぜか私を選んでくれた。その理由は相手の方に聞いてみないとわかりませんが、とてもラッキーだったと思います。</dd><br>
