<h4 class="interview_detail_subtitle">パートナー　片野 大輔氏</h4>

             
             <div class="content-lead">20代後半から30代前半のコンサルティングファーム出身者や投資銀行出身者が集い、高い志を掲げて2011年に設立された株式会社YCP Japan（旧ヤマトキャピタルパートナーズ）。経営コンサルタントとしてキャリアを積んでいた片野氏も、そこに参画した一人である。片野氏は、どんな思いでこちらに加わり、ここで何を成し遂げようとしているのか。</div>
             
             
            	<div class="clear green b content_title_view">自分たちのブランドで事業をやりたい。そんな思いからYCPグループへ。</div>
            		<dl>
    <dt>入江</dt>
    <dd>片野さんがYCPグループに入社されるまでのご経歴を教えてください。</dd>
    <dt>片野</dt>
    <dd>東京大学の工学部を卒業後、新卒でドリームインキュベータに入社し、そちらで戦略コンサルティングとベンチャー投資や経営支援に3年半ほど携わりました。その後、ボストン・コンサルティング・グループに移籍し、引き続きコンサルティング業務を担い、2014年からこちらに参画しています。</dd><br>

    <dt>入江</dt>
    <dd>新卒でドリームインキュベータを就職先に選ばれたのは、どのようなお考えからですか。</dd>
    <dt>片野</dt>
    <dd>もともとコンサルティング業界に関心がありました。私は父親が事業を営んでいたこともあり、学生時代から「経営」を身近に感じていましたし、またサークルやボランティア団体のリーダーを務めた経験を通して、人を動かして組織を変えていくことにも興味がありました。大手のグローバルファームに進む選択肢もありましたが、私が就職活動を行っていた2000年代半ばの頃は、国内で有力なベンチャーが続々と誕生していて、リスクを取って投資家に近い目線で事業に関わる経験もしてみたいという思いもあり、戦略コンサルとベンチャー投資、その両方を学べるドリームインキュベータに魅力を感じて入社したのです。</dd><br>

    <dt>入江</dt>
    <dd>その後、どういう経緯でボストン・コンサルティング・グループに移られたのですか。</dd>
    <dt>片野</dt>
    <dd>私が社会に出てしばらくしてリーマンショックが起こり、ベンチャー市場が急速に萎みました。それにともない、より多くの大企業に対するコンサルティングを経験したい考えで、ボストン・コンサルティング・グループに移りました。もともとドリームインキュベータはボストン・コンサルティング・グループの出身者が立ち上げたファームであり、カルチャーギャップもそれほどないだろうと。</dd><br>
   
   
     <dt>入江</dt>
    <dd>ボストン・コンサルティング・グループでは、どのような案件を？</dd>
    <dt>片野</dt>
    <dd>リテールや消費財、通信・メディア企業などをクライアントに、ビジョン策定や個別の戦略構築および実行支援などに携わりました。事業計画構築、新規事業立上げ、M&amp;A戦略立案、営業戦略構築など、さまざまなコンサルティングに関わりました。
</dd><br>

 <dt>入江</dt>
    <dd>ドリームインキュベータとボストン・コンサルティング・グループという、性格の異なる2つのファームで経験を積まれて、片野さん自身が得たこととは何ですか。</dd>
    <dt>片野</dt>
    <dd>ドリームインキュベータは、純粋に企業の戦略を考えるプロジェクトが中心のファームであり、経営戦略を立案する力は養われたように思います。また、ベンチャーインキュベーションにも関わっていましたので、投資家としてビジネスモデルや経営者とどう評価するか、という視点も得られました。一方、ボストン・コンサルティング・グループは、描いた戦略をクライアントが自ら実行に移すレベルまで踏み込んでいく。誰にどのようなコミュニケーションを取れば大企業が動くのか、そうした現場でのプロジェクト遂行力を鍛えられました。</dd><br>


<dt>入江</dt>
