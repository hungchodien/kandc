    <dd>端的に言えば、自分たちのブランドで事業をやりたい、という思いからです。実は、YCPを創業した代表の石田は大学時代の私の先輩で、当時から交流があり「いつかは一緒に事業をやろう」という話をしていました。彼から誘いを受けて、経営陣の一角としてこちらに参画しました。</dd><br>

