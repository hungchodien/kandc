             <h3 class="interview_detail_title">株式会社野村総合研究所</h3>
<h4 class="interview_detail_subtitle"><span style="font-size:12px;">コンサルティング事業本部 事業推進コンサルティンググループ グループマネジャー</span><br>西野 貴弘氏<br><span style="font-size:12px;">コンサルティング事業本部 ビジネス・テクノロジーグループ 副主任</span><br>秦 伸太朗氏</h4>

             
             <div class="content-lead">日本初の民間総合シンクタンクである野村総合研究所（NRI）。近年はコンサルティングの領域にも事業を大きく拡げ、経営コンサルにおいてはすでに国内で確固たるポジションを築き上げている。他のファームとNRIの違いとは何か？ そしてNRIに参画する意義や魅力とは？ キャリア入社の二人のコンサルタントの方に、話をうかがった。</div>
            	<div class="clear green b content_title_view">NRIは発展途上。まだまだ事業や組織を自らの手で作れる可能性がある。</div>
            		<dl>
    <dt>入江</dt>
    <dd>お二人ともNRIにキャリア入社されたとのことですが、それぞれご経歴を教えていただけますか。</dd>
    <dt>西野</dt>
    <dd>私はNRIで4社目です。SIerでのERPコンサルタントからキャリアをスタートし、その後、コンサルティングファームを2社経験して、2011年にNRIに入社しました。</dd>
    <dt>秦</dt>
    <dd>私は新卒で2010年に国内の大手コンサルティング会社に入社し、SAPの会計領域の導入コンサルに3年半ほど携わった後、NRIに転職しました。</dd>
    <br>
    
   <dt>入江</dt>
    <dd>西野さんは、どのような経緯でNRIに移られたのですか。</dd>
    <dt>西野</dt>
    <dd>実は、当初は事業会社を転職先として考えていました。長らくファームに在籍していたこともあり、今度は自ら事業や組織の成長に関与したい、できれば腰を据えてじっくりとキャリアを重ねていきたいと考えていたのです。NRIはこれまで同様にコンサルティングというビジネスを行いながら、希望していた今後のキャリアを積む場として大変魅力を覚えました。</dd>
    <br>
    
    <dt>入江</dt>
    <dd>その時ご自身が感じられたNRIの魅力とは、具体的にどのようなことですか。</dd>
    <dt>西野</dt>
    <dd>端的に言えば、NRIはコンサルティングファームとして成長し続けているということです。政策提言などのリサーチから、企業の事業戦略などのコンサルティングに事業の軸足を移して20年程とまだ歴史は浅いのですが、すでに国内では外資の有力な戦略系ファームとも互角に戦えるポジションにまで昇りつめています。その一方で、グローバル展開できる余地はまだ大いに残されていますし、コンサルティングにおいても、事業戦略立案だけではなく実行支援まで担おうと、今まさにサービスの領域を広げている段階。ファームとしての成長余力が多分にあると感じました。そして、外資系ファームの日本法人とは異なり、東京の丸の内がヘッドクォーターですから、あらゆることが自社内で意思決定できる。ここなら前職でのケイパビリティを活かしながら、事業や組織を自力で作り上げていく醍醐味も味わえるのではないかと考えたのです。</dd>
    <br>
    
   <dt>入江</dt>
    <dd>すでに豊富なキャリアをお持ちの西野さんにとっても、NRIはさらなる成長や挑戦の機会がある場だとお感じになられたのですね。一方、秦さんはなぜNRIに転職されたのですか。</dd>
    <dt>秦</dt>
    <dd>前職ではSAPの会計領域の導入コンサルを手がけていましたので、特定のパッケージを扱い、求められる専門知識も会計に限られていました。20代で自分のコンサルタントとしてのキャリアをあまりに特化してしまうのもどうかという思いが次第に募り、若いうちはもっと上流で幅広い案件を経験してみたいとNRIへ転職しました。</dd>
    <br>
     
   <dt>入江</dt>
    <dd>NRIは社内にシステム部門を抱えていらっしゃるため、コンサルティングもSIに繋げていかなければならないのではないか、と思っている方も多いようです。秦さんはいかがでしたか？</dd>
    <dt>秦</dt>
    <dd>確かに世間ではそう思われている向きもあるようですが、実際はそうした縛りはまったくありません。私が入社後籍を置いているのは、前職の経験が活かせる経営情報コンサルティング部という所で、クライアントのシステムの上流の構想策定などの案件も多く手掛ける部署なのですが、実際にベンダーを選定する際、NRIのシステム部門はその選択肢の1つではありますが、あくまで中立的な立場で「何がクライアントにとって本当のバリューなのか」という視点でコンサルティングができます。外資系の大手ファームのIT系コンサルタントは自社SIに繋げるのが命題ですが、NRIはいい意味でそれが全くない。私にとっては逆にそれが驚きでしたね。</dd>
