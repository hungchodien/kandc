    <dt>西野</dt>
    <dd>確かに、「型にはまっていない」というのはNRIの魅力の1つですね。他のファームはマトリックス型の体制を敷いているところが多いと思いますが、NRIはアメーバ型と言われており、関われる案件の自由度が高い。私自身も以前は製造業や総合商社の案件を主に手がけていたのですが、NRIに入社してからは特にその縛りはなく業務を行っています。</dd>
