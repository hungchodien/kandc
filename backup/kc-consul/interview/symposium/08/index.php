             <h3 class="interview_detail_title">トーマツ ベンチャーサポート株式会社</h3>
<h4 class="interview_detail_subtitle">アドバイザリーサービス事業部 瀬川 友史氏&事業開発部 森山 大器氏<br/></h4>


             <div class="content-lead">デロイト トーマツ グループのネットワークを活用し、全国2,500社を超えるベンチャー企業の成長支援を行うトーマツ ベンチャーサポート。さらにその基盤の上に、大企業とベンチャーとの協業による新規事業創出支援や大企業の中で挑戦するイントレプレナーの支援、官公庁・全国自治体のベンチャー政策の立案・実行支援なども繰り広げている。そんな同社を新たなキャリアの場として選んだ、外資系戦略ファームと日系シンクタンク出身者の二人に話をうかがった。</div>
            	<div class="clear green b content_title_view">ここに身を置けば、自ら“オープンイノベーション”の推進役となれる。</div>
            		<dl>
    <dt>入江</dt>
    <dd>まずは、トーマツ ベンチャーサポートに入社されるまでのご経歴を教えていただけますか。</dd>
    <dt>森山</dt>
    <dd>私は東京大学工学部で大学院まで応用物理を学び、卒業後に新卒でボストンコンサルティンググループ（BCG）に入社しました。BCGには6年間在籍しましたが、最後の1年間は休職してシカゴのイリノイ工科大学にあるデザインスクールに留学。というのも、経営コンサルタントとして数々の案件に携わるうちに問題意識が芽生えてきたんですね。長期的な目線で見ると、やはり新規事業を創出していかなければクライアントの持続的な成長は望めないのではないか、と。こうした領域で自分の知識を深めたいと、ビジネスデザインの教育に定評のあるイリノイ工科大学への留学を決意したのです。そして現地で1年間学んで帰国後、当社に参画しました。</dd>
    <dt>瀬川</dt>
    <dd>私も東京大学工学部の出身で、そのまま大学院の修士課程に進んでロボット工学を専攻しました。若い頃から「技術で夢のある社会を創りたい」という志を抱き、近未来を見据えてロボットを研究テーマに選んだのですが、学会などでいろんな研究者の方と触れ合うと、みなさん研究のことばかり考えていらっしゃるんですね。当たり前と言えば当たり前のことなんですが、私はそれが少し気になって……「こんな未来を実現できる」と提示するだけで、それ以上の具体的な進展がない。それは研究者サイドの問題ではなく、技術と社会をつなぐプロフェショナルがいないからだと考え、では自分がその役割を担おうと。それで博士課程に進むのをやめて就職活動を始めたのですが、そこで出会ったのが“シンクタンク”という存在でした。なかでも科学技術に強い大手総合シンクタンクがあると知り、ここなら技術と社会をつなぐ仕事ができるのではないかと入社したのです。同社には当社に転職するまで9年間在籍し、先端技術開発に関する国の産業政策を手がけたり、また民間企業での技術起点での新規事業開発などに携わりました。</dd>
   
    
    <dt>入江</dt>
    <dd>森山さんが転職先としてトーマツ ベンチャーサポートを選んだのは、どのようなお考えからですか。</dd>
    <dt>森山</dt>
    <dd>先にお話ししました通り、私が志向していたのは「新しい事業を創り出す」ということ。しかも、長い目で見て「日本にイノベーションが次々と起こるようなエコシステムを創りたい」という思いがありました。アメリカに留学して感じたのは、向こうでは既にイノベーションコンサルティングという業態が存在しており、イノベーション創出に特化したコンサルタントが大企業のクライアントに深く関与して社内から新規事業を生み出すのが一般的。一方、日本では、会社の重要な意思決定に関わる検討を外部にアウトソースするのは依然として抵抗感が強い。それもイノベーションにつながるような、やってみなければわからない不確実性の高い領域はなおのことです。いわゆるこれまでの“経営コンサルティング”では、自分のやりたいことを実現するのは難しいと思ったんですね。では、日本にふさわしいイノベーションの起こし方とは何か？を考えると、それはやはり“オープンイノベーション”ではないかと。社内でゼロから新しいものを起こすというよりも、既に存在しているイノベーションの種を社外から持ち込んで触発させる。その手段としては事業提携かもしれないし、買収かもしれませんが、こうしたスタイルのほうがきっと日本の大企業には合っている。ベンチャー一社では起こせるイノベーションは小さいかもしれませんが、大企業と組み合わせてレバレッジすれば社会への大きなインパクトに繋がる。</dd>
    <dt>入江</dt>
    <dd>森山さんが掲げる “オープンイノベーション”を実現できる場が、トーマツ ベンチャーサポートにあったと？</dd>
    <dt>森山</dt>
    <dd>ええ。当社は非常に面白い構造を持ったコンサルティングファームでありシンクタンクだと思います。ベンチャー企業への経営サポートも手がければ、大企業の新規事業創出の支援も手がけており、また官公庁や地方自治体の政策面での支援にも関わっています。オープンイノベーションを実際に起こすためにはベンチャーと大企業の双方を理解した上でうまくつなぐということが必要ですが、本当の意味で双方にアプローチできている組織はなかなか存在しないでしょうし、バックボーンが監査法人で投資も行わないという中立的な立ち位置だからこそ、行政やメディアとも関わりやすい。加えて、デロイト トーマツ グループには監査・コンサル・ファイナンシャルアドバイザリーなどの様々なファンクション間で風通し良く協業できる文化とグローバルネットワークがある。これらの構造的にユニークな点を知った時に、新しい事業を生み出すイノベーションファームが創れるとしたら世界中を見渡してもここしかないと思いました。</dd>
