             <h3 class="interview_detail_title">プライスウォーターハウスクーパース株式会社</h3>
<h4 class="interview_detail_subtitle">事業再生サービス シニアマネジャー　田中 基興氏<br>事業再生サービス シニアマネジャー　野村 泰史氏<br>トランザクションサービス シニアアソシエイト　田中 秀樹氏</h4>

             
             
             <div class="content-lead">プライスウォーターハウスクーパース ディールアドバイザリー部門はM&A、事業再生のパイオニア的な存在として知られ、数多くの案件を手がけてきた。今回はその核である事業再生サービスチームと、企業買収や事業部門の売却、合併ならびに戦略的業務提携を支援するトランザクションサービスチームの3人にご登場いただき、それぞれのキャリアヒストリーと業務内容についてお話をうかがった。</div>
            	<div class="clear green b content_title_view">我々の役割は「企業の命を救うER」</div>
            		<dl>
    <dt>入江</dt>
    <dd>最初に、皆さんのこれまでのキャリアについて教えてください。</dd>
    <dt>田中基</dt>
    <dd>新卒で総合電機メーカーに入社し、所属は経理部だったのですが事業の経営計画や戦略立案など手広く事業管理を担当していました。そこに6年いた後に銀行系シンクタンクのコンサルティング部門、外資系の戦略コンサルで「何でも屋」的に幅広いテーマのコンサルティングに従事し、08年にプライスウォーターハウスクーパース(PwC)に入社しました。大企業に比べ、キャリアとしてはリスクの高いプロフェッショナルの世界へ移ったのは、20代後半で感じがちな「現状に対する焦燥感」と社内コンサル的な仕事での成果に充実感を感じたからです。</dd>
    <br>
    
    <dt>入江</dt>
    <dd>他のファームを2社経験して、御社に移られたのはなぜですか。</dd>
   <dt>田中基</dt>
    <dd>「何でも屋」的に幅広く経営コンサルティングをやっていると、引出しの多いコンサルタントになっていくのですが、その延長として総合力を問われる「事業再生」に興味を持つのは自然な帰結でした。また、コンサルティングファームに転職した時の最も大きな動機が、先ほど申し上げた「事業の建て直し」での充実感でしたので、「事業再生」に対して、漠然とではありますが関心を持っていました。PwCを選んだのは、事業再生コンサルティングの世界で、「最も歴史が長く」「ノウハウが集積し」、その結果としてのブランド力、すなわち「築き上げられた顧客からの信頼」があるためです。</dd>
    <dt>野村</dt>
    <dd>私はシンクタンク系のコンサルティングファームに10年弱在籍した後、08年にPwCへ入社しました。前職の業務内容はシステムインテグレーションの上流工程も多く、ITを使った新規事業の立ち上げや、複数の会社がITを活用してアライアンスを組むといった案件を中心に担当しましたし、ナショナルプロジェクトも多かったですね。そうした「世の中にないサービスをつくる仕事」は面白く、やりがいもあったのですが、10年近くやっているうちに、企業経営の深刻な局面で自分は何ができるだろうと考えたのが転職のきっかけでした。そこでM&amp;Aや事業再生で自分が何かできそうなところを探し、PwCに入社しました。</dd>
     <br>
    
    <dt>入江</dt>
    <dd>お二人とも事業再生という、コンサルの中でも大変なテーマに興味があったのですね。</dd>      
    <dt>野村</dt>
    <dd>我々のクライアントは売上が数千億円規模の大企業で、かつ明日には潰れるかもしれない会社も多いです。従業員1000人、家族も含めると4000人が路頭に迷うかもしれない局面で、どんな手を打てるか社長や経営陣とギリギリの所で検討を行い、実行に移していく緊張感はあります。成功するかどうかは分からない、だけど我々が手を引けば確実に会社が潰れる局面で出来ることを全てやり切るのはやりがいがありますし、ある程度決着がついたときにクライアントの社長から本音で「ありがとうございました」と言ってもらえるのは一般的な経営コンサルティングではできない経験です。</dd>
    <dt>田中基</dt>
    <dd>事業再生コンサルティングが他のコンサルティングテーマと異なる点は、クライアント企業にとって我々が「最後の砦」ということです。病院で例えるなら「ER（救急救命室）」であって、生きるか死ぬかの瀬戸際の企業と対峙し、命を救う役割をしています。なので、必要なことは何でもやる。大きな絵を描くのは当然ですが、金融機関をまわってお金を集めたり、事業部門の売却先を探してきたりもする。投資銀行やM&amp;Aの専門家がやるような領域もまとめてやるんです。PwCには様々な領域の専門家がいるので、我々事業再生チームがコアとなり、生き残りの術を組み立て、専門家達と共に矢継ぎ早に実行していく。非常に大変な仕事ですが、普通では味わえない充実感があります。</dd>
     <br>
    
    <dt>入江</dt>
    <dd>トランザクションサービスの田中さんのご経歴はいかがでしょうか。</dd>
   <dt>田中秀</dt>
    <dd>私の場合は、新卒で外資系の戦略コンサルティングファームに入社し約3年間在籍した後、外資系プライベートエクイティファンドで約3年間働き、今年の6月にPwCへ転職しました。1回目の転職理由は、戦略コンサルティングの仕事は事業戦略に与えるインパクトは大きい反面、必ずしも顧客の経営課題全てを対象としている訳ではないので、私としては戦略という一つの領域に特化するのではなくより幅広く企業経営というものを学びたかったからです。その観点から、企業を丸ごと買収するプライベートエクイティファンドでの仕事は、企業経営上必要なことは全てやらなければならず、幅広くスキル・経験を身に付けられるのが非常に魅力的でした。</dd>
     <br>
     
     <dt>入江</dt>
    <dd>そこから御社へ転職されたのはなぜですか。</dd>
   <dt>田中秀</dt>
    <dd>日本企業のグローバリゼーションという課題に対して、より深く取り組みたくなったというのが一番大きいです。自分のキャリアでの経験を鑑みると、日本企業がぶつかる最大の経営課題はグローバリゼーションです。大企業でもベンチャー企業でも、日本である程度の成功を収めると海外展開による更なる成長が視野に入りますが、実際に日本企業が海外でビジネスを成功させるのは本当にハードルが高い。私は、なんとか日本企業が海外で成功するための支援をできないかと思う一方で、そのために必要なスキル・経験が不足していることも感じていました。PwCへ入社したのは、そのような折に「クロスボーダー案件のチームを強化したい」という話を頂いたからです。もともと前職でPwCにはお世話になっていたこともあって会社の雰囲気はある程度知っていましたし、一緒に仕事をしたい人達だとかねてから考えていたのも非常に大きかったです。</dd>
