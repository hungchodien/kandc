            		<dl>
  <dt>入江</dt>
    <dd>それぞれのチームにおける最近のプロジェクトの傾向はありますか。</dd>
    <dt>田中基</dt>
    <dd>「事業再生」では大企業の案件が増えています。人口が停滞している日本では、これまでと同じ数の企業がやっていけないのは明らかです。そこで業界再編が必要な業界や、収益モデルを見直す必要のある企業が増えています。特に業界再編が必要な場合、必然的に大企業が対象になるというのも、大企業の案件が増えている背景です。また、明日にも資金繰り破たんが見えている企業ばかりでなく、数年後の破たんが予期できる企業の案件も増えています。成熟マーケットで、数十年来収益モデルを変えられなかった企業が、数年後にどういう状況に陥っているのか？はある程度読めるので、事業基盤の劣化初期に手当の必要を感じた金融機関と企業からの引き合いが増えています。そのため、取り組む課題も従来からの「バランスシートを外科的治療で立て直すこと」に加え、「反転攻勢と持続的成長に向け、いかに収益モデルを改めるか？」に比重を置いた案件が増えています。</dd>
    
    <dt>野村</dt>
    <dd>海外展開のご相談も多いです。大企業はすでに海外進出しているところが多いのでそこの売上をどう伸ばすか、各国マーケットの動向がどのように変化していて、そのなかで子会社をどうすべきか。あるいは現地の会社を買収し子会社にしたいといったご相談もあります。こうした話は皆さんが想像する以上にたくさんあります。</dd>
   <dt>田中秀</dt>
    <dd>トランザクションサービスでも状況は似ていると感じていて、マクロ的な事業環境の変化に伴う事業再編やクロスボーダー案件、ノンコア事業の売却案件等が増加しています。成長の見込めない国内市場で消耗戦をしていてもメリットがないことは自明であり、多くの業界で今後も更なる再編は避けられないように感じます。一方で、今後の事業成長の軸を海外市場に求めているクライアントが多く、東南アジアや中国、インドなどの成長市場へ積極的に出ていこうとする案件が増えています。加えて、グローバリゼーションに伴う海外企業との競争激化に伴い、本業と明確なシナジーの無いノンコア事業の売却を決断されるケースも後を絶ちません。</dd>
     <br>
    
    <dt>入江</dt>
    <dd>コンサルタント経験者で御社に向いているのはどんな人ですか。</dd>      
    <dt>田中基</dt>
    <dd>事業再生に必要なスキルは、いわゆる戦略コンサルファームをはじめ、数多のコンサルティングファームの持つスキルの多くを包含しているので、転職してきた人にとってコンサルタントとしてのスキルの質と幅は、確実に広がります。また、先ほど申した通り、組織経営者としての視座を経験できるので、経営系ポジションを目指す人の鍛錬の場とも言えます。経営コンサルタントとしてキャリアを磨きたい方にも、経営系キャリアを積みたい方にも、最適なフィールドです。長期停滞の続く日本には、「チャレンジ精神」と「逃げない姿勢」で、戦い続けられる人間が求められています。そういう人間を目指す人には最適な「フィールドの広さ」と「解決すべき課題の困難性」のあるファームです。志を持って、腕っぷしを磨きたい人には最適な場所だと思っています。</dd>
    <dt>田中秀</dt>
    <dd>PwCに向いているのは、本当の意味で経営者と同じ目線になり、同じだけの幅で経営課題ととらえ、同じだけの深さで実行したい人ではないでしょうか。戦略コンサルティングファームでは戦略・実行計画が描けた段階で、「後はこれをやってくださいね」と渡して仕事は終わりで、実行の深さが極めて浅い。しかし、自分が本当に経営者なら戦略だけを考えていてはダメで、人事、財務、ITなど複雑に絡み合った経営課題も含めて、実際に現場のオペレーションを変えていかなければなりません。PwCでは、そうした幅広い経営課題に対する極めて深いレベルでの実行という点で、戦略コンサルティングでは経験し得ないプロジェクトが圧倒的に多い。もちろん、戦略にフォーカスしたキャリアパスも当然あって、それを望む人は戦略ファームでキャリアを進めるのがよいと思いますが、幅広い経営課題に対して、現場レベルまで深く入って解決したいという人はPwCに向いていると思います。</dd>
        <dt>野村</dt>
    <dd>大企業にはMBAホルダーも戦略コンサル出身者もいますから、考えることだけならどの会社もできると思います。それでも実行に移せないのは何らかの理由があるからですが、我々事業再生チームが入る局面ではそうした「出来ない理由」も一つずつ解決し、考えたことをちゃんと実行に移していきます。面白いと思いますよ、自分の描いた絵がどんな結果になるのか、最後まで見られますから。よく「コンサルタントは提案して終わり」という言い方がされますが、本当に苦しんでいる会社をなんとかして、再成長の軌道にのせる仕事は他では身に付かない能力が身に付きます。その分、しんどいのは確かですが（笑）。</dd>
    <dt>入江</dt>
    <dd>本当に大変そうではありますが（笑）、中長期的にコンサルタントとしてキャリアを積んでいくことを検討されている方にとっては、経営に必要なスキルを広く深く身につけられ、他のコンサルティングではなかなか味わえない使命感や達成感を感じながら仕事が出来るフィールドなのですね。本日は貴重なお話を聞かせて頂きまして、誠にありがとうございました。皆さまの今後のご活躍を影ながらお祈りしております。</dd>   
    
    
