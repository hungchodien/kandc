<p>事業再生サービス シニアマネジャー　田中 基興氏<br>事業再生サービス シニアマネジャー　野村 泰史氏<br>トランザクションサービス シニアアソシエイト　田中 秀樹氏</p><br><br>
            	<div class="clear green b content_title_view">「本当の経営者目線」で物事を見て、実行する</div>
            		<dl>
    <dt>入江</dt>
    <dd>皆さんコンサルティングファームから転職されてきましたが、前職と比較して特徴的に感じたことを教えてください。</dd>
    <dt>田中基</dt>
    <dd>もっとも特徴的なのは、経営者の視座に立ったリーダー行動を求められることです。PwCに入社する前も、「経営者の視座で」ということは先輩コンサルタントからよく言われていたので、そのつもりでアウトプットしておりましたが、事業再生の世界を一度経験すると、本当の経営者の視座ではなかったことがよく分かりました。経営者の基本行動とは、自らwillを打ち立て、will実現のためにステークホルダーを説得し、その理解の上で会社を動かし実現していく、そして「逃げられない」ことです。どんな会社でも実際にトップにならないと絶対にそういう目線で仕事はできません。なぜなら、組織には分権化された自分の担当領域と、より上位の責任者がいるからです。ところが、我々の仕事では、まさにステークホルダー全体を見渡し、説得し、あるべき姿に向かって動かしていく、さらに「最後の砦」として「逃げられない」、という経営者と同様のリーダー行動が求められるのです。加えて特徴的なのは、経営者として求められる抽象度の高い思考に加え、施策実行の推進役として具体性の高い思考と実務レベルでのリーダーシップが要求されることです。粗っぽい括りですが、抽象度の高いところは戦略コンサルの世界、具体度の高いところは業務コンサルの世界とすれば、我々はこの「思考の抽象度のダイナミックレンジ」とスキルの「専門性の深さ」において、戦略コンサルと業務コンサルの両方を備えている必要があります。PwCの事業再生チームで、最も特徴的に感じているのは、今述べた二つ、「高い視座に立ったリーダーとしての行動」「極めて広い領域で戦えるだけのコンサルスキル」が求められることです。</dd>
    
    <dt>野村</dt>
    <dd>入社して驚いたのは時間軸です。一週間以内にステークホルダーを説得しないと会社がどうなるがわからない局面があったり、ほとんどのクライアントが上場企業なので、監査期間の間に適正意見が出ないと信用を失ってしまうが、まだ必要な計画が準備できていない場合があるなど、時間軸が非常にタイトなんです。一般的なコンサル案件だと3か月で報告書を作成するという動きが多いですが、それよりはるかに濃密な動き方をします。同時に、プロジェクト1件あたりの期間が長い。私がやった案件の一つは1年半、もう一つは2年で、この時間軸はいままでに経験がありません。クライアントの危機局面からプロジェクトを開始し、しっかりと再生を果たすまでサポートしようとすると、それだけの時間が必要になってきます。もう一つ驚いたのは案件の規模です。クライアントはほぼ大企業ばかりとは言え、プロジェクトによっては従業員の他、出資者、銀行、弁護士、会計士、税理士、コンサルなど100人も200人もの関与者が短期間で動きます。我々はその中心に立ち、クライアントの再生に向けて必要な検討内容を整理し、関係者に必要なタスクを割り振り、進捗を管理する等、全てさばいていかないといけません。これだけの規模の案件を扱えるのは、PwC以外ではほとんどないと思います。</dd>
   <dt>田中秀</dt>
    <dd>PwCが他社と比べて圧倒的に違っている点は大きく二つあると思います。一つは、対象とする領域の幅の広さと深さです。我々のトランザクションサービスチームでは、M&amp;Aにおけるファイナンス・デューデリジェンスを中心にクライアントをサポートしていますが、クライアントは事業戦略やストラクチャリング、タックスやシステムなど、非常に幅広い視点でのアドバイザリーを必要としています。そのとき、戦略コンサルティングファームでは企業の事業計画など特定領域にしか対応できませんが、PwCは基本的にあらゆる領域でのアドバイザリーを提供することができます。PwCには多種多様な専門性を持った人たちが社内にいるので、自分にできないことであっても社内でチームを組むことにより、結果的にクライアントが必要としているアドバイザリーをPwCは全て提供できるのです。もう一つは、圧倒的なグローバルのカバレッジです。PwCは世界158ヵ国に18万人の従業員がいるので、基本的に世界のあらゆる地域に対応できるのですが、この規模のカバレッジを持つコンサルティング企業はPwC以外には存在しません。今日もあるクライアントと中央アジアの小さな国の案件について議論してきたのですが、日本企業が海外で事業展開する際に我々がお手伝いできる地域および経営領域の広さと深さは他社とは比べ物になりません。</dd>
     <br>
    
    <dt>入江</dt>
    <dd>どの領域にも対応できる総合性は一方で、「何でもできるは何もできない」という懸念にもつながると思います。その点はいかがですか。</dd>      
    <dt>田中基</dt>
    <dd>PwCが「何でもできる」と言い切れるのは、事業再生チームが各専門部隊の横串として機能しているからです。ありがちなのは、スキルの百貨店であっても、実際はスキル別組織の縦割りによって、総合的に機能しないケースです。このような場合、横串組織を立ち上げ、組織合力の最大化を図るのが一般的ですが、横串組織の発言力が弱かったり、横串組織が利益責任を負わないバーチャル組織であったりで、掛け声倒れになることが多々あります。PwCの事業再生チームは99年にPwCのアドバイザリー部門が設立された時の発祥チームです。つまり、事業再生サービスを軸に、現在のコーポレートファイナンスやトランザクション、M&amp;A支援などといったサービスを拡充してきた面もあるため、事業再生チームが橋渡しとして機能し、企業経営にかかるあらゆるディールおよびコンサルティングサービスを一体で提供できるのです。「何でもできる」が機能しないと、クライアント企業が破たんしてしまいますし・・・。</dd>
    <dt>野村</dt>
    <dd>事業再生チームとトランザクションチームがチームアップするといった仕事はよくあります。たとえば、事業再生で非コア事業を売却するという局面では、トランザクションチームはM&amp;A関連業務の経験が非常に豊富ですから、どう対応すべきかを考えるうえで非常に心強いし、PwCがクライアントから評価されるポイントでもあります。</dd>
    
