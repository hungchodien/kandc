    <dd>まず取り組んだのは、“STEP ZERO”から関わるという我々が描くプロジェクトの理想を具現化していくことです。そうした成功事例をいくつも作り上げていくことに入社以来私は努めてきました。たとえば、いま携わっている日系エアラインの案件もそのひとつです。すでに数年来のおつきあいになりますが、これほど長期に渡ってクライアントと関わるケースはそう多くはないのではないでしょうか。我々に課せられたテーマはマーケティング。ただ、ただのマーケティングプロジェクトというわけではなく、プロジェクトが終わった後も社内にケイパビリティがある人・部署ができ、自走することができる状態をつくることでした。自走するということは、それを担う人材や仕組みを構築していかなければならず、そのテーマ自体が非常にチャレンジングなプロジェクトだったと思います。このプロジェクトを可能にしたのは、まさに、フィールドマネージメントのクライアントとの付き合い方にあるといっても過言ではないでしょう。プロジェクトの進め方の試行錯誤を立上げ前から含めクライアントと一緒に行い、本当にプロジェクト化することが必要なものだけを選び出すと同時に、それ以外のものに関してどのようにサポートしていくかを喧々諤々議論しました。この全体設計の段階からクライアントとコミュニケーションを行い、実行までをサポートするという、まさにコンサルティングの理想的な姿だと思います。</dd>
