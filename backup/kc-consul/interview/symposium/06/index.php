             <h3 class="interview_detail_title">株式会社フィールドマネージメント</h3>
<h4 class="interview_detail_subtitle">ディレクター　吉本 貴志氏＆小林 傑氏＆竹森 大介氏</h4>


             <div class="content-lead">日本のコンサルティングのあり方を変革し、自らが掲げるビジョンを追求すべく設立されたフィールドマネージメント。プロフェショナルファームからこちらに参画してきた3名に、転職の経緯や現在取り組んでいるテーマ、そしてフィールドマネージメントでキャリアを積む醍醐味などについて話をうかがった。</div>
            	<div class="clear green b content_title_view">以前のファームで抱いていた悩みや葛藤を、ここでなら解消できる。</div>
            		<dl>
    <dt>入江</dt>
    <dd>みなさん、以前もコンサルタントとしてさまざまなプロフェッショナルファームでキャリアを積まれていたとのことですが、まずはこちらに移られた経緯を教えていただけますか。</dd>
    <dt>吉本</dt>
    <dd>私は以前、マッキンゼーに在籍していました。そちらで最終的にプロジェクトマネージャーを務めさせていただいていましたが、内心、葛藤を感じていたところもありました。というのも、グローバルなファームであるがゆえ、どうしても本部の方針に沿った行動が求められます。私は国内のある業界を専門にコンサルティングしていたのですが、その業界の日本企業のプレゼンスが弱まってくると、「グローバルのトップ企業にアプローチする」というファームの方針に沿って、従来おつきあいしていたクライアントとの距離を置かざるを得なくなってしまうケースが出てきます。せっかく結果を出せており、良いサイクルが回り始めていたとしても、関係が維持できなくなってしまうため、非常に心苦しく感じていました。そんな折、フィールドマネージメントの存在をしり、興味を覚えました。代表の並木に会って話をしてみると、ここでは“STEP ZERO”というコンセプトを掲げて、案件が発生する前から経営者の相談相手になり、密に議論しながらプロジェクトを作り込んでいき、その実行部分のサポートも手厚いスタイルをとっていることを知りました。コンサルティングの理想の形を、本気で追求していることに感銘を受けたのが、こちらに移った大きな理由ですね。</dd>
    <dt>小林</dt>
    <dd>私は以前、リンクアンドモチベーションにて組織人事領域のコンサルティングを行っていました。大手企業を中心に組織開発や人材開発の案件を数多く手がけましたが、この領域のコンサルティングは、ビジョンや戦略に基づいて組織をつくり人材を育てていくため、手間と時間がかかるわりに成果が目に見えにくいことが特徴です。プロジェクトが終わるとクライアントから感謝いただけるものの、我々のコンサルティングが果たしてどの程度クライアントの収益向上に貢献しているのかを測ることは難しい。そのため「上流の戦略から組織をつくり実行するところまでを通してコンサルティングできたら面白いだろうな」という思いを持っていました。その後、知り合いを通じて並木に出会ったのですが、彼は私とは逆に上流側での問題意識を抱いていました。経営コンサルティングは、成果が出るまで継続できない、実行するメンバーに意志がないなどでうまくいかないことが多く、心からクライアントに感謝されるプロジェクトは決して多いとは言えないと聞きました。もっと経営者に寄り添って実行までを支援していきたいというフィールドマネージメントの目指す方向が、私が望むコンサルティングの形とマッチしていると感じて、参画しようと決意しました。</dd>
    <dt>竹森</dt>
    <dd>私は以前ボストン コンサルティング グループに在籍していましたが、戦略立案からその実行支援まで幅広くサポートをしていました。約10年間在籍し、様々な業界の様々な案件に携わってきましたが、その中で、次のキャリアは事業開発や新規事業創造に関わっていきたいという想いを強く持つに至りました。ファームを辞めた直後は、自ら事業開発/スタートアップに携わることを目指し、3つのアクションをとりたいと考えていました。ひとつは、ベンチャーを始めとした事業のトップ・経営者100人と議論し、新しい視点や事業の芽を探すこと、二つ目は、こうした議論を通じて10案程度の事業プランを練り上げ、そのうち絞り込んだ3つで具体的な行動をおこしてみること、そして三つ目は、こうした事業開発を実現する仲間やプロフェッショナルが集う場を創るということ。そんなことをテーマに掲げて活動する中で、数人目に出会った経営者が並木でした。フィールドマネージメントは、コンサルティングファームでありながら、自社で事業会社を立ち上げてすでにバイアウトした実績もある、そしてイノベーションに挑むトップファームで研鑽を積んだプロフェッショナルが何人も在籍するユニークな集団に映りました。事業開発の経験も自らの武器にしたいという並木の考え方にフィットするところがあって、参画を決めた次第です。</dd>
