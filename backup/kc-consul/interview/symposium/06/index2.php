    <dd>事業開発や経営に関わることが、我々のコンサルティングの価値を高めることにつながっていくんですね。私たちは「ハイブリッド型」と呼んでいますが、経営経験のあるコンサルタントの集合体となることを目指しています。経営者の気持ちは経営者にしか分からない。だからこそ、それがフィールドマネージメントの強みとなり、STEP ZEROに近づいていける。誰かが事業を創って経営を担うことでハイブリッドな人材が増え、結果としてフィールドマネージメント全体の価値も向上していく。このサイクルをうまく回せているファームというのは他には無いと思いますね。</dd>
