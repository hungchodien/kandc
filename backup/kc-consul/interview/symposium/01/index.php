            		<dl>
    <dt>入江</dt>
    <dd>最初にお二人のこれまでのキャリアについて教えて下さい。</dd>
    <dt>阿部</dt>
    <dd>私は前職もコンサルティングファームで、その会社には新卒で入社しました。システム開発寄りの傾向が強い会社で、メインフレームのプログラミングからはじまって、だんだん業務・システム要件定義や基本構想といった上流工程を経験するようになり、前職での最後のほうはERPのコンサルタントとして活動するようになりました。そして2004年にプライスウォーターハウスクーパース株式会社の前身であるベリングポイント株式会社に転職しました。実は転職前、一緒にERP関連の導入案件にサブコンとして入ったことがあり、中でどんな仕事をしているかはイメージできている状態でした。入社当時は、ERP導入案件が多かったのですが、最近はそこから派生して管理会計や組織再編の案件を手がけています。</dd>
    <dt>中内</dt>
    <dd>私はもともと総合商社の出身で、新卒での入社当初より海外投資案件の担当としてM&amp;Aやファイナンシャル・リストラクチャリング業務に従事していました。しかし97年のアジア通貨危機の影響を受け大きな配置転換があったことをきっかけに、将来のキャリア設計を考え直すこととなりました。海外投資案件の担当時代、外資系投資銀行や監査法人、弁護士などプロフェッショナルスキルを持ったカウンターパートから刺激を受け、自分もそうした環境に身を置きたいと考え会計系のコンサルティングファームに転職しました。そこでERPの導入に従事した後、2003年にベリングポイントに入社しました。入社当時はERPと管理会計を専門としていましたが、プロジェクト経験を重ねる中でリスク管理やプロジェクトマネジメントの方にキャリアの幅を広げていき、現在は組織再編のプロジェクトマネジメントを中心に持株会社や地域統括会社設立に伴う新組織設計、ガバナンス体制の再構築などを担当しています。</dd>
    <br>

   
   <dt>入江</dt>
    <dd>御社に転職する決め手は何でしたか。</dd>
    <dt>阿部</dt>
    <dd>転職前もERP案件に従事していて、もう少しこの仕事をやっていきたいとの思いを持っていました。しかし前職ではERP案件があまり大規模なプロジェクトにはならず、会社の方向性としてもERPビジネスは縮小傾向だったので、一緒にプロジェクトを行った経験のある当社に転職することに決めました。</dd>
    <dt>中内</dt>
    <dd>私は前職のファームが新卒採用を中心にしている会社で、中途入社の人間がキャリアの王道に乗って行くには少しハードルが高いと感じたのが再度の転職を考えるきっかけでした。当社を選んだのは、面接官から「経験を積みビジネスマンとして成熟した中途入社の人材が活躍する場にしたい」というメッセージを聞き、この会社なら多様なバックグラウンドを持つ同僚と一緒に自分も成長しながら仕事ができるだろうと思ったからです。</dd>
    <br>
    
    
    
    <dt>入江</dt>
    <dd>プライスウォーターハウスクーパースはどんなカルチャーや風土を持ったファームだとお感じになっていますか。また、入社前のイメージとのギャップはありますか。</dd>
    <dt>阿部</dt>
    <dd>社長の椎名は同じフロアで働いており、いまだにプロジェクトに出入りするなど非常に現場に近いところにいます。そんな風にとても風通しがよく、実際、社長やパートナーに対しても言いたいことは何でも言え、自由な社風の会社だと思います。ギャップについては入社前に一緒に仕事をしていたことがあるのであまりないですが、当時のマネージングディレクター（現在のパートナークラス）にとても若い人がいて、前職の会社とは大きく違うなとびっくりしたことがあります。</dd>
    <dt>中内</dt>
    <dd>自由というのはその通りですね。年次にかかわらず「私はこの分野に興味があり、専門にしたい」と望むキャリアを主張することが許される会社です。もちろんその自由は自律性とセットのもので、自己研鑽のないものにはリターンもないことはハッキリしています。イメージのギャップといいますか、入社前と比べて予想以上だったのは、サービス領域が非常に幅広い点です。会計士資格の保有者が会計基準を踏まえた専門サービスを提供していたり、サプライチェーンやCRMもERPという切り口だけではなく戦略や業務改革といった切り口でコンサルティングをしていたり。前職ではERPの部隊が大きく会計系ファームはそういうものだと思い込んでいましたが、当社では幅広くクライアントのニーズに応えていく体制ができており、とても印象的だったことを今でも覚えています。</dd>
   
   
   
