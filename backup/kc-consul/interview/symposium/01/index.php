             <h3 class="interview_detail_title">プライスウォーターハウスクーパース株式会社</h3>
<h4 class="interview_detail_subtitle">シニアマネージャー　阿部 大祐氏&amp;中内 聡子氏</h4>
             <div class="content-lead">コンサルティングとディールアドバイザリーを提供する国内最大規模のコンサルティングファームであるプライスウォーターハウスクーパース。そこではどんな人物が活躍し、どんなキャリアを形成する機会があるのだろうか。今回はコンサルティング部門のシニアマネージャー2人にご登場いただき、入社の経緯や業務内容、成長の機会などをテーマに語ってもらった。</div>
