<p>シニアマネージャー　阿部 大祐氏&amp;中内 聡子氏</p><br><br>
            	<div class="clear green b content_title_view">納得できる人事評価のために議論を尽くす</div>
            		<dl>
   <dt>永田</dt>
    <dd>コンサルティングファームを志望される方のなかには評価体系を気にする方もいらっしゃるのですが、どのような仕組みになっていますか。</dd>
    <dt>阿部</dt>
    <dd>まずプロジェクトマネージャーが各メンバーの評価を行います。以前はプロジェクトマネージャー一人の考えで評価をつけていて、同じパフォーマンスでも評価が甘いまたは厳しいマネージャーといて不公平感がありました。最近はある程度以上の規模のプロジェクトであれば複数のマネージャーがいるので、プロジェクト内部で評価ミーティングを開催して、できるだけ皆が納得できるような評価を行う文化がきちんと浸透してきました。</dd>
    <dt>中内</dt>
    <dd>カリブレーションのステップがプロジェクト内、チーム内、さらに全社という形で行われているので、特定のチームやパートナーの下で評価が異なるといったことは起きにくくなっています。コンサルタントに求める要件も、仕事の姿勢、専門スキル、クライアントや同僚とのコミュニケーション、リーダーシップといった項目が設定され、タイトルごとにマトリックスで整理されています。ですので「なんとなく頑張っている」というような抽象的な評価をするのではなく、「君が優れているのはこの項目で、課題はこの項目」と評価者・被評価者相互に共通した評価軸をもって具体的なフィードバックを行える制度になっています。</dd>
    <br>



<dt>永田</dt>
    <dd>人事評価を行うマネージャー以上の方はかなり大変ですね。</dd>
    <dt>阿部</dt>
    <dd>評価を徹底して行うようになった当初は本当に大変でした。最近はきちんと回るようになってきて、しかも皆が納得できる評価を共有できるようになったと思います。</dd>
    <dt>中内</dt>
    <dd>コンサルティングファームは人が商品です。やはりアソシエイト、シニアアソシエイトが現場の売りです。したがって彼ら彼女らが「自分の頑張りはきちんと評価してもらえる」と思って仕事ができるようにしないとプロジェクトは続きませんし、クライアントから評価を得られず結果的に会社の根幹も揺らぎかねません。「ダメなところはダメと指摘するけれど、やったことはちゃんと見ていますよ」とわかってもらうことが本当に大事です。</dd>
    <br>



<dt>入江</dt>
    <dd>お二人とも9年、10年と一つのファームにお勤めです。コンサルティング業界としては長期間で、その背景にはPwCならではの魅力があると思うのですが、それは何ですか。 </dd>
    <dt>阿部</dt>
    <dd>特定の専門領域だけでなくいろいろなことができるチャンスの多い会社なので、バランスよく自分のスキルを広げたり深めたりできるのが一つの魅力です。それはアソシエイトだけでなく、マネージャー以上になっても同じです。たとえばアサインミーティングには当然、マネージャーのリクエストも来ます。通常、その分野の未経験者をマネージャーに起用するのはかなりハードルが高いですが、当社では本人の能力を考慮し任せることもあります。</dd>
    <dt>中内</dt>
    <dd>時には「えっ、私がこれを!?」という驚きのアサインもあります。実際に私自身、ERPから組織再編へのキャリアシフトを経て、社内に確実にチャンスが存在すると身をもって体験しています。この先もグローバル領域などへのチャレンジを目指しており、できるかどうかは自分次第ですが、また新しいチャンスを見いだせるだろうという可能性が感じられる会社だと思っています。</dd>
    <br>



<dt>入江</dt>
    <dd>お話をおうかがいしていると、内部にたくさんの機会がある会社という印象を受けました。これから、5年、10年先のキャリアをどのように描いていますか。</dd>
    <dt>阿部</dt>
    <dd>この会社の中で自分のやれる領域を広げていきたいと思っています。今後はグローバルの案件にも関わっていきたいですし、部門横断のプロジェクトの経験もまだ少ないので、それらの領域をさらに広げていきたいと考えています。</dd>
    <dt>中内</dt>
    <dd>PwCは世界158カ国にネットワークがありますが、その中でビジネスコンサルタントとして伍していけるプロフェッショナルでありたいというのが今後5年間の目標です。私は10年間当社にいますが、この組織は生き物で変化し続けていますし、昇進の都度視野も広がりますので、やり尽くした感はまだ全くないです。「この会社でやりたいことはもうないな」と思うまでは、当社でのキャリアを楽しみたいと思います。</dd>
    <br>

   
