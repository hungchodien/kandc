        <h4><img src="<?php echo url_root; ?>img/blog/post-title_007.png" alt="コンサル業界のトレンドと採用マーケットの変化" /></h4>
        <div class="content_view_top clear">
        <p class="clear">
先月、Newspicsで連載されていた『変貌するコンサル業界　3つの新トレンド』という特集があり、堀紘一氏やBCG代表の御立氏、A.T.カーニー代表の岸田氏の他、コンサルティング業界の第一線で活躍されている現役コンサルタントからOBまで、数多くの方が今のコンサルティング業界について語られていて、非常に示唆に富んでいて面白かったです。<br><br>

既に読まれた方も多いもしれませんが、コンサルタントのキャリアの面から参考になりそうなポイントをまとめてみました。<br><br>

――――――――<br>
経営共創基盤（IGPI）パートナー・塩野誠氏が語られているコンサルの新潮流は「PE化」「高級文房具化」「フルライン化」の3つ。<br><br>

「PE化（VC化）」<br>
長期的・持続的な企業価値の向上を目的として、直接的に資本を入れてリスクを共有しながら人材投入型の成長支援を行う。<br>
※IGPI、DI、リヴァンプ、YCP、フィールドマネージメントなどが行っている形態ですね。<br><br>

「高級文房具化」<br>
ミドル相手のプロジェクトの多くは、カウンターになる顧客が経営者に対して行う戦略立案とプレゼンテーションの資料作成代行となってきている。<br>
※外資系戦略のTOPファームでもこのような仕事が増えてきていると聞きます。<br>
<br>
「フルライン化」<br>
M&Aでいえば、戦略立案からデューデリジェンス（資産価値の評価）、M&Aアドバイザリー、PMI（統合後の組織体制の構築）など、一連のプロセスをすべてカバーする。<br>
※昨年、総合系ファームのPWCが、戦略系ファームのブーズと経営統合し、社名をストラテジー・アンドに変更しましたが、これもその一環ですね。
<br><br>
他には、コンサルティング業界がクリエイティブ路線へのシフトやデジタル化が進んでいるとのこと。<br><br>

今年5月にマッキンゼーがデザインに特化したコンサルティング会社のLUNARを買収。アクセンチュアも英国のデザイン会社であるフィヨルドを買収した。<br><br>

コンサルタントの300枚の資料が、トップクリエーターの「モノ」に負ける時代。<br>
例えば、佐藤可士和さんのようなトップクリエーターがモノ自体を提示したら、300枚の資料よりずっと雄弁であり、クライアントはそちらのほうに飛びつくはず。<br><br>

今後のコンサルティングファーム像について熟考した結果、クリエイティブにも明るくなければ、クライアントを説得できない。これからはロジカルとクリエイティブ、両方の感性を持つことが大事とのこと。<br><br>

デジタル面では、BCGは「BCGデジタル・ベンチャー」を立ち上げ、アクセンチュアは、頭文字をとって「SMACS（スマックス）」と言い、ソーシャル、モバイル、アナリティクス（統計）、クラウド、センサーを強化している。
※記事で取り上げられてはいませんが、デロイト、NRI、NTTデータ経営研究所などでもデジタル分野は強化しています。<br><br>

「朝起きると、その日のオススメのコーディネートが鏡に映る。<br>
　通勤途中の駅で目にするのは動画広告だ。<br>
　色やサイズを選び、「BUY NOW」ボタンをプッシュ。<br>
　たとえば街を歩く女性の服が気になれば、デジタル端末でスキャン、<br>
　自動的にオンラインショッピングサイトの購入画面へ──。<br>
　そんな未来のデジタル社会を描いたこのムービー。」<br><br>

これは、アクセンチュアとクライアントのファーストリテイリングがデジタル分野で協業していくにあたり世界観をイメージして制作したビデオ。コンサルティング会社が、広告代理店のようなサービスを提供し始めている。<br>
――――――――<br><br>

コンサルティング会社も事業会社と同様、時代の流れやトレンドに合わせて少しずつ業態を変えています。その流れで、採用活動においても、広告代理店やクリエイティブ製作会社出身者やWEBエンジニア、データサイエンティストを採用するようになってきています。<br>
総合系ファームが戦略ファームを買収したり、コンサル会社がデザインファームを買収する流れがありますが、今後、更なる業界再編があると思います。<br>
コンサルタントはそのような中でどの分野で専門性を築き、先を見据え、エッジを立てていくべきか、を考えないといけません。<br><br>

特集の最後の連載記事で堀紘一氏が語られていた下記のコメントが深いと思いました。<br><br>

「あらゆる仕事で一番大切なのは問題発見能力なんだよ。問題がきちんと定義できれば、<br>
解決策は直ちに思いつかなくても、いずれ出てくるし、自分でわからなければ知恵のある人に聞いて回ればいい。そうなると解決するのは時間の問題なんだ。」<br><br>

問題解決力ではなく、問題発見能力の方が重要だと。<br>
新聞社、商社でキャリアを積んだ後に、長年第一線でコンサルティングの仕事をしてこられた方ならではのコメントで、深く、重く、説得力があると感じました。<br><br>


</p>
