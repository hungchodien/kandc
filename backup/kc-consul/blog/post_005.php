        <div class="content_view_top clear">
        <p class="clear">
私はここ数年、コンサルタントのキャリアインタビューを積極的に行っております。毎回、「コンサルタントの仕事の醍醐味って何でしょうか？」と聞かせていただくのですが、先日お話を伺ったコンサルタントの方は、「そうですね、先日ある経営者の方に、『貴方と一緒に仕事ができてとても楽しかった』と言われたのは凄く嬉しかったですね。」とおっしゃっいました。<br><br>

一瞬ありふれた言葉のようにも聞こえますが、私にとってはとても新鮮であり、コンサルタント冥利に尽きるとても良い話だと思いました。<br><br>

コンサルティングの仕事は、自社だけでは解決できない難しい問題をテーマにして、その問題の肝は何かを“一緒”に考え、時には現場の反対を“一緒”に説得したりしながら、ある一定の期間、濃密な時間を過ごすことになります。また、クライアント側にとっては、これまで全く考えもしなかった新たな気付きや知的な刺激を与えてもらったりと、純粋に『一緒に仕事ができて楽しい』と感じることがあるのでしょう。<br><br>

先日、グローバルトップファームにおいて最年少で執行役員に昇進されたトップコンサルタントの方とお話をした際に、「なぜ貴方に大企業の経営者は仕事を依頼し続けるのでしょうか？」と聞いたところ、少しおどけながら「チャーミングなところですかね（笑）」とおっしゃっていました。<br><br>

この方とはこれまでに何度かお会いしたことがあるのですが、毎回笑いの絶えない楽しい時間になります。コンサルタントとして一流の実力があることは間違いないのですが、それに加えて、『一緒に仕事をしていて楽しい』と感じるからこそ、継続的に依頼を受けるのではないかと感じました。<br><br>

「私は“企業”ではなく“人（経営者）”と仕事をすることを常に意識しており、更に言えば、仕事だけでなく、プライベートでも何か相談したいことがあったら社内の同僚よりも真っ先に自分のところに相談してもらえる関係性を築くことを目指している。」とおっしゃっていたのが印象的でした。<br><br>

ある外資系戦略ファームの採用責任者の方は、「コンサルタントの仕事は究極の接客業ですから、お客様から好かれるタイプではないと感じたら、いくら能力があっても採用しない」と断言されていました。<br><br>

コンサルタントは難易度の高い適性試験を実施し、ケーススタディを通して高度な論理思考能力を求められますが、ベーススキルとして、『一緒に仕事がしたい』と思わせる人間性もとても大事な要素であると、コンサルティング業界の様々な方とお会いする中で強く感じました。<br><br>

ちなみに上記で取り上げたトップコンサルタントのインタビュー記事がありますので、宜しければ、ご覧ください。<br>
（記事はこちら：<a href="http://kc-consul.com/interview/top/05/" target="_blank" style="text-decoration:underline">http://kc-consul.com/interview/top/05/</a>）<br><br>

