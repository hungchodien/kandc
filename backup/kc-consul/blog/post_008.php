        <p class="clear">
このブログをご覧の皆さまの中には「いつ転職をしたら良いか」と考える方もいらっしゃるのではないでしょうか。<br><br>

現役コンサルタントの方からは「プロモーションをしてから転職すべきか」「マネージャーになってから転職すべきか」という質問をいただくこともしばしばあります。その答えは人それぞれですし、様々な切り口がありますが、今回は基本的な考え方として2つの観点をお伝えします。<br><br>

●現職で活躍している／不安・不満にちゃんとケリを付けている<br>
前提として、現職で活躍しているかどうかや、転職したくなるほどの不満や職場の問題点を解決するために自分ができることを十分に果たしたかは重要なポイントになります。<br>
詳細については、弊社代表：丸山のコラム<a href="http://diamond.jp/articles/-/39387" target="_blank" rel="nofollow">「“逃げ出し転職”は絶対失敗する！転職に向いている時・いない時の見極め方」</a>をご覧いただきたいです。<br><br>

●やりたいことが明確になっている（あるいは、新天地にやりたいことがありそうでワクワクしている）<br>
前回、「自分のやりたいことが明確ですか？」というテーマでブログを書きましたが、やりたいことが既に明確になっていれば、あるいは明確になっていなくても新天地にやりたいことがありそうでワクワク感があれば早めの転職をお勧めします。やりたいことに向かって積む経験は無駄にはなりませんし、それが本当にやりたいことかどうかも実際にやってみなければ分からないと思うからです。<br><br>

やってみた結果、本当にやりたいことだったということであれば、とても幸せなことですし、もしも自分の想像と異なっていたという場合であっても、早期に気付けて良かったと言えます。<br><br>

先日、外資系戦略コンサルティングファームを経て、事業会社の社長室へ入社し、現在は主力事業部の部長をされている方にインタビューをさせていただく機会があり、コンサルタントの転職時期についても話が及びました。コンサルタントの方は、事業会社への入り方に関して慎重になると思うし、キャリアパスやプロモーションを含めてその方自身も真剣に考えたということでしたが、転職をしてみると、事業会社への入り方を考えることはある程度大事だと思うものの、細かく考えても思った通りにはいかないため、領域や会社として楽しめるのであればそこに賭けてみて、万が一、上手くいかなくてもしょうがないと感じるようになったということです。<br><br>

起こるかもしれないことをあれこれと考えて、上手くいかない確率を数％低くすることに時間を使い、転職が1年遅れる方が、時間の使い方としてもったいないのではというお話もあり、強く共感しました。<br><br>

転職時期を考える参考となれば幸いです。<br><br>


