こんにちは。キャリアコンサルタントの永田です。<br><br>

今回は「コンサルティングファームで得られるもの」について
お話させていただきたいと思います。<br><br>

毎日たくさんの方にお会いさせていただいていますが、
コンサルタントを志望する方からは、
「コンサルティングファームで何を得られるのか？」、
一方、現役のコンサルタントの方からは
「自分の何を評価してもらえるのか」といった
質問をいただくことがあります。<br><br>


これまで、コンサルタントを経て事業会社へ転職された方や、
現役コンサルタントの方に弊社WEB企画でインタビューをさせていただく
機会がよくあるのですが、みなさんのお話をお伺いしていると
下記のような答えがありました。<br><br>


　●「何が問題なのか」を考える力<br>
　●問いを立て、答えていく能力<br>
　●とことん考え抜く力<br>
　●メッセージ（ビジョン）を表現する力<br>
　●周囲の関係者を巻き込んでいく能力<br>
　●経営者の視座<br>
　●ビジネスへの感度・対応力<br>
　●「うまくいかないことはたくさんあるんだ」という一種の達観と柔軟性<br><br>


これらは、トランスファラブルスキルと呼ばれる、どの企業でも活かせるもので、
今後、ますます重要視されると思います。<br><br>


先日、株式会社JVCケンウッド　取締役　執行役員 最高戦略責任者（CSO）の
田村氏にインタビューをさせていただきました。<br>
（記事はこちら：<a href="http://kc-consul.com/interview/consultant-post/09/" target="_blank" style="text-decoration:underline">http://kc-consul.com/interview/consultant-post/09/</a>）<br><br>


その中で
『優秀なコンサルタントは、そもそも“AかBか”は課題の本質ではなくて・・・、と更に思考を深めていきます。
 “A”を主張する自分、“A”を否定する自分、課題設定そのものを見直す自分。
私はよく「3人の自分をもて」と表現するのですが、そうした思考を養っておくべき』<br><br>

というお話をお聞きしました。<br><br>

このお話から、同じ時間を過ごす場合も、
意識の持ち方次第で得られるものの大きさは変わると感じました。<br><br>

現役コンサルタントの方は参考にしていただければ、
より良いキャリアが築けるのではないでしょうか。<br>

少しでも参考になれば幸いです。<br><br>

