        <div class="content_view_top clear">
        <p class="clear">
前回に引き続き、コンサルタントを経て、事業会社の社長、役員、及びPEやVCの代表になられた方々に共通するポイントの第二弾をお伝えいたします。<br><br>

＜若いうちにリスクを取れるか＞<br>
コンサルタントを経て、事業会社の社長、役員、及びPEやVCの代表になられた9名の中で、その内2名はファームでパートナー経験がありますが、他の7名の方はその前に何かしらのチャレンジをしています。退路を断ち自費でMBAを取得したり、起業したり、ベンチャーに飛び込まれたりしています。<br><br>

私の感覚では、以前とは異なり、コンサルタント経験者は非常に増えているので、30代後半からコンサルティング経験のみで事業会社の経営層、もしくはその候補として転職することはかなり難しくなってきていると感じています。<br><br>

我々が事業会社で経営幹部の求人依頼を頂く際に、「コンサル経験者は歓迎ですが、コンサル経験者の場合は、事業会社側でのご経験があると尚可です」と言われることが増えてきています。<br><br>

ちなみにアマゾン社はコンサル出身者が多数活躍されていますが、同社VPの渡部氏は以下のように仰っておられます。<br><br>

<div style="border-top:dotted 1px #666; width:100%; height:1px;"></div><br>
自分が苦労したところから考えると、転職はリスクの少ない形でしたほうがよいと思います。コンサルタントが事業会社へ移ろうとすると、同じ仕事内容ではやはり給料レベルは下がります。そこで無理に同じ水準を保とうとするとスキルセットが合わずアップアップしてしまう。<br>
特に、コンサルタントは、自身が事業責任を持った経験がないことを忘れ、事業会社ではすぐに対面にいた経営者や事業部長になれるように期待しがちです。奥さんに「同じ給料をもらえないでどうするの！」と言われているのかもしれませんが、ならば「これは自分が成長するためのワンクッションである」と説得しなければいけません。<br>
自分の能力を発揮できるようになったら正当に評価され、その後はちゃんと上がっていくからと。<br>
<div style="border-top:dotted 1px #666; width:100%; height:1px; margin-top:20px;"></div><br><br>

上記のコメントでは少し報酬面についても語られていますが、これまでインタビューさせていただいた9名の方々も似たようなことを仰っていました。<br><br>

大事なキャリアの意思決定の場面で、他社から現職よりも高い金額でオファーをいただいたが、そちらを選択していないということです。つまり、報酬面を重視して、意思決定をされていないということです。<br><br>

特にコンサルタントから事業会社へ転職する場合は、大抵の場合、年収面がネックとなりますが、若い時の方が年収ダウンの幅を押さえて転職できると思います。<br><br>

ちなみに、上記で9名のうち、2名の方はパートナー経験があるとお伝えしましたが、そのお二方とも、事業会社の経営幹部を目指すのであれば、少しでも早く事業会社に移った方が良いと仰っておられました。<br><br>

事業会社で活きる経験としては、マネージャー経験まで積めれば十分だと言えるようです。ただ、お二方共に人とのご縁で今のポジションに就かれているので、パートナーまで経験することで、人脈形成の利点はあるのかもしれません。
