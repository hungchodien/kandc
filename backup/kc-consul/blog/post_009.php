        <p class="clear">
弊社で本サイトを始めてから約4年が経ち、これまでに44名のインタビューを実施しました。その中でコンサルタントを経て、事業会社の社長、役員、及びPEやVCの代表になられた方々が9名います。その方々のインタビューを通して、どのようなキャリアを積むことで経営者になられたか、共通するポイントがあるように思い、そのポイントを3つほどまとめてみました。<br><br>

今回はまずその1つ目をお話させていただきます。<br><br>

＜ご縁でキャリアを切り開く＞<br>
9名の経営者のうち、6名がコンサルファームに在籍中、もしくは仕事を通じて知り合った方とのご縁がきっかけで今のポジションに就かれていました。人のご縁でキャリアを積んでこられた中で最も代表的な方はCITICキャピタル代表の中野氏です。<br><br>

中野氏のインタビューで最も印象的だったコメントを抜粋します。<br><br>

<div style="border-top:dotted 1px #666; width:100%; height:1px;"></div><br>
私のキャリアは最終目的をハッキリして歩いてきたわけではありません。その時々のチャンスとご縁でいろいろな選択をしてきたのですが、結果として見るとその時代の先端をいく分野の仕事ばかりやってきました。それは自分で時代を切り拓いてきたというより、時代を切り拓いている人とのご縁があったからです。<br><br>

アドバンテッジではリチャード、産業再生機構には冨山さん。そういう時代の先端を走っている人に私は引っ張ってもらいました。要は新しいもの好きで怖いもの見たさがあって、「これから」というものにすごく興味が湧きます。戦略的に考えるというより、先端的なことをやっている人たちの話を聞き、「なるほど！」と思ったことをやってきました。<br><br>

そして、私を引っ張ってくれた方々は皆、能力も人格も素晴らしい人たちです。そういう人たちとのご縁を大切にすることが、やはり一番の秘訣かもしれません。声をかけられたからといって、誰にでも付いていっていいわけではないですよ（笑）。能力だけある人は世の中にいっぱいいますが、能力も人格も優れていて、かつビジョンを持っている人はあまりいません。普通はそうした人物とはなかなかご縁を持てませんが、私は幸運なことにご縁を持たせていただき、チャンスを与えてもらった。世の中には私程度の人間は他にもたくさんいますが、なぜか私を選んでくれた。その理由は相手の方に聞いてみないとわかりませんが、とてもラッキーだったと思います。<br>
<div style="border-top:dotted 1px #666; width:100%; height:1px; margin-top:20px;"></div><br><br>

中野氏は時代の最先端を走っていて、能力だけでなく、人格も優れていて、ビジョンを持っている人を大切にすべきだと仰っておられます。ただ、そのような方々の回りには当然優秀な方々がたくさんいるはずです。日々の業務を通して、そのような方々の目に留まるような仕事をし続ける事が経営者になるための一番の近道なのかもしれません。<br><br>

中野氏のインタビューの全文は以下からご覧いただけます。<br>
<a href="http://www.kc-consul.com/interview/consultant-post/01/" target="_blank">http://www.kc-consul.com/interview/consultant-post/01/</a><br><br>

