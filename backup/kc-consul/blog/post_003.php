        <p class="clear">
先日、官民ファンド出身者のAさんとお会いする機会があり、Aさんから官民ファンドで働く魅力について新しい気付きを頂きました。<br><br>

Aさんの場合、官民ファンドに移ることを決めた最大理由は時限立法であることだったようです。<br>
※参考までに、既に解散している産業再生機構は当初5年間限定の時限立法でした。<br>
結果的には1年早い4年間で解散。<br><br>

一見、時限立法の場合、その後のキャリアが不安になるのでは？<br>
と思われますが、Aさんの場合はその逆の発想をお持ちでした。<br><br>

ある一定期間限定の特別な組織に、政府機関のほか、民間企業からは金融機関、コンサルティングファーム、監査法人、事業会社から一斉に優秀な人材が集まり、あるタイミングで一斉に散らばる。<br><br>

短期間にこれだけ多方面で人脈を築くことはこの先一生ないのではないかと思って、このチャンスは絶対に逃したくないと喜んで参画されたようです。その後、Aさんは再生支援先企業の代表を務めるチャンスにも恵まれました。<br><br>

また、Aさんから官民ファンドと民間ファンドの違いについても教えて頂きました。<br><br>

民間ファンドの場合、出資を受け入れる経営者の心理として、<br>
極端に言えば、どうせ安く買われて、数年後に高く売られて、逃げられてしまうのだろうと思われやすい。<br>
※所謂ハゲタカファンドと言われるような印象。<br><br>

一方で、官民ファンドの場合、<br>
政府が絡んでいることもあって、もっとクリーンなイメージがあり、国を挙げて自社を助けてくれるという受け止め方もあり、大抵の経営者は好意的に話を聞いてくれるそうです。<br><br>

そのこともあって、官民ファンドに在籍することで所属ファンド内の人脈はもちろんのこと、外部の様々な経営者との人脈も一気に広がったようです。<br><br>

Aさんは既に同ファンドを辞められていらっしゃいますが、未だに経営者になるお誘いの話をいただくようです。<br><br>

実際に官民ファンド出身者で経営者や経営メンバーになられている方は沢山いらっしゃいます。<br><br>

Aさんとは別の官民ファンド出身者で、こんな話をして頂いた方もいらっしゃいました。<br><br>

「コンサルを極めても経営の道には繋がらない。経営を目指すのであれば、コンサルティング＋αの要素が必要で、それは、実際に事業会社側での実務経験と経営者との人脈が重要である。」と仰っていました。<br><br>

官民ファンドでは見事に上記2つの要素を得られます。<br><br>

コンサルタントとの面談では、ゆくゆくは経営者を目指したいというご相談を受ける機会が沢山ありますが、Aさん、Bさんのお話を受けて、経営者を目指すのであれば、官民ファンドでキャリアを築くことも有力な選択肢の一つとして考えても良いかなと思いました。<br><br>

</p>
