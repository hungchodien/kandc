        		<p>Z氏にふさわしい仕事と転職先を私たちが検討し、候補にあがったのが多岐にわたる事業展開を行っている上場企業C社でした。C社はIT関連事業を展開しており今後更に強化したい意思を持っていましたが、我々の立場から組織全体を眺めると事業を大きく発展させるための人材が不足していました。実績の豊富なZ氏がジョインすればC社の成長に貢献できる可能性が高い。そう見込みをつけて、早速Z氏に打診したところ、「成長性の高い、とても面白い会社ですね」という前向きな答えが返ってきました。<br><br>

Z氏の栄転話が進んでいたため一刻も早くマッチングを成立させなければいけないため、すぐに作戦を練り、動き出しました。<br><br>

Z氏をこれまでの実績に応じたポジションや給与で採用する意思があるかどうか、名前も具体的な社歴も明かさずに、採用人事を飛び越えて副社長に一気にジャッジしてもらわなければ時間的に間に合わないこと。また、その上で海外出張が多い社長との面接をセットしなければならないことなど、いくつもの困難を乗り越え、奇跡的に海外子会社への栄転が本決まりになる直前にZ氏の転職が決定しました。まさにギリギリのタイミングでのマッチング成立。Z氏も私たちも歓喜しました。<br><br>

「本当にきわどいタイミングでしたが、日本で働き続けることができるようになってハッピーです。しかも、ポジションも報酬も考え得る最高の条件でした」<br>
先日、C社に移ったZ氏にお会いしたところ、そう笑顔で語っていただきました。<br><br>
</p>
        
