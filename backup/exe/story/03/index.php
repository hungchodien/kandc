         <div class="right_content_1 clear">
        		<p>ある週末、以前に当社で転職のお手伝いをし、現在は外資系IT企業で活躍している方から連絡がありました。休日に何かあったのだろうかと電話をとってみると、用件は「とあるエグゼクティブが動く可能性があるので、ぜひ紹介したい。ただし名前は当日まで一切明かせない」とのことでした。<br><br>

後日、会食の場をセッティングしてお会いしてみると、依頼者は紹介者が勤務する会社の日本法人の社長Z氏でした。Z氏は部下である紹介者に「転職を含めて次のキャリアを考えているが、情報が漏れるのは困る。信頼のおけるエグゼクティブコンサルタントがいたら紹介して欲しい」と相談していました。そこで紹介者の頭に思い浮かんだのが以前、ご自身の転職で利用した当社だったのです。<br><br>

そのときZ氏には勤務している外資系IT企業グループ内の、別の海外子会社への栄転話が進められていましたが、40代半ばのZ氏にはご家庭の事情があり、「できれば日本で働き続けたい」という意向を持っていました。しかも栄転の話を会社が着々と進めているので、転職するならその正式決定の前にマッチングを成立させなければいけないという話で、動ける時間は限られていました。<br><br>

名の知られた企業のVIPクラスなら転職活動も容易と思われるかもしれませんが、実はかなりの困難を伴います。まず、周囲へ与える影響が大きいため、情報が絶対に漏れないよう秘密裡に動かなければなりません。また、普段企業から出される人材募集にはトップ経験者に適合する案件はほとんどなく、その人に合ったポジションをゼロから開拓していくというアプローチが必要になります。つまり、その方にふさわしいポジションと待遇を企業につくってもらわなければいけないのです。しかも取締役は退職後の競業避止義務を結んでいることが多いため、同じ業種へ転職することはできません。このケースでは時間的な制約もあります。<br><br>

Z氏は他の人材紹介会社とも面談したうえで、私たちに「一任するので手伝ってほしい」とおっしゃってくださいました。
</p>
        
