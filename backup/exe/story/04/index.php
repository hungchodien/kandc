         <div class="right_content_1 clear">
        		<p>大手外資系メーカーの経営企画部門責任者を務めていたO氏とはじめてお会いしたのは1年前のことでした。これまで複数の外資系企業で成果を出してきたO氏には、すでに同業他社のヘッドハンターからもさまざまなオファーが届いており、本人も転職の希望をお持ちでしたが「なかなか自分に合ったポジションが見つからない」というのが悩みでした。<br><br>

50代に入ったO氏の希望条件をお伺いすると「現在の業種に近い企業のCFOで、報酬は2000万円以上」でした。しかし、この条件を満たす企業を具体的に考えるとかなり限られているため、実現の可能性は低いと思われました。<br><br>

では、どんなポジションならO氏に合うのか。よく考えてみると、そもそもO氏が語っている希望条件では、従来と同じような会社の同じような仕事に就くことになります。そろそろキャリアの総仕上げの時期に差しかかった方のキャリア選択としては本当にそれでよいのだろうか、と感じました。<br><br>

そこで、こんな質問をしてみました。<br>
「これからの50代をどう過ごされたいですか。どんなことにワクワクしますか？」<br><br>

少し考えたあと、O氏はこう答えました。<br>
「家族との過ごし方も含めて、これまでとはまったく異なる働き方をするのもよいかもしれません。スタートアップベンチャーも意外と面白いかな。でも、報酬の水準は下がりますよね……」<br><br>

確かにベンチャー企業でCFOになったとしても、報酬はO氏の求める水準にはほど遠いのが現実です。しかし本人が希望条件として語った内容とは裏腹に、O氏の言葉の端々には「今までとは違う新しいチャレンジがしたい」という気持ちが感じられました。そもそもO氏の現在の地位は、アグレッシブにチャレンジを繰り返して勝ち得たものです。
</p>
        
