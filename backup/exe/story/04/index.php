     <article class="story_content_wrap clear">
     	 
         <h3 class="left_content_1 align_left clear">
         <img class="pc_img" src="<?php echo url_root; ?>img/story/vol04_sub_title_02.png" alt="通常ならあり得ないマッチングで新しいチャレンジの機会を提供" />
          <!--<img class="mobile_img" src="<?php echo url_root; ?>img/story/vol04_sub_title_02_mobile.png" alt="実力者を必要としているポジションを見抜きクライアント企業にアプローチ" />-->
          
         </h3>
         
         <div class="right_content_1 clear">
        		<p>そこで私たちがO氏に候補先企業として提案したのが、テクノロジーベンチャーのD社でした。D社は社長が30代とまだ若く、その分野ではトップクラスの技術者が集結している非常に有望な会社です。しかし組織体制の整備はこれからという段階にあり、ファイナンスの専門家も社内にはいませんでした。<br><br>

「現在の業種に近い企業のCFO」という当初のご希望とはだいぶかけ離れていますし、通常ならあり得ないマッチングかもしれません。しかしこのタイミングで豊富な経験を持つO氏がジョインすれば、D社の成長に大きく貢献できる可能性があります。D社は海外進出も計画していたので、そこでもグローバル企業で活躍してきたO氏の知見を活かせるでしょう。同時に、O氏のチャレンジ欲は確実に満たされます。<br><br>

「有望なベンチャーのCFOポジションがあります。ただし、年収は下がります」<br><br>

O氏にそう提案するとあまり色よい反応ではありませんでしたが、「せっかくご紹介いただいたのだから」と、面談をご希望されました。実際にお会いしてもらうと、当初の反応とは裏腹にO氏はD社社長の誠実な人柄にすっかり魅了されました。<br><br>

「自分よりずっと若いけれどこの社長は尊敬できる。将来有望なベンチャーの成長に貢献する仕事にチャレンジするのも面白そうだ」とO氏は考え始めましたが、やはり条件面でなかなか決断できずにいました。<br><br>

そうこうしている間に、他の候補者が採用されることになり、O氏とは一度、破談になってしまいました。ところが半年後、どんでん返しが起こります。まず、久しぶりにO氏からこんな連絡がありました。<br><br>

「その後も転職先を探しているが、心からよいと思える会社とまだ出会えていません。思い返すとD社を逃したのは本当にもったいなかった。そこでもう一度、ベンチャーも含め候補企業を提案してもらえないでしょうか」<br><br>

O氏の要望のなかにはD社への未練が色濃く感じられました。そこで私たちは改めて、<br>
「それほど心残りがあるならもう一度だけ、D社に打診してみましょう。それでだめなら、他の新しい候補企業を探します」とお伝えし、D社への再アプローチが始まりました。<br><br>

いくつかのラッキーも重なり、奇跡的にCFOのポジションがまだ空席のままでした。最終的に相思相愛となりめでたくO氏の入社が決定しました。<br><br>

「振り出しに戻って考えてみると、やりたいことは新しいチャレンジです。若くて素晴らしい経営者を支えるのは50代の過ごし方としてとてもいい仕事だなと思いました」<br><br>

D氏はそう生き生きとした表情で語りました。
<br><br>
</p>
        
