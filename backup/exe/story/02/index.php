　弊社とベンチャー企業B社との出会いは知人からの紹介によるもので、人材採用のオーダーを受けたわけでも営業をかけたわけでもありませんでした。最初にB社の社長と会ったときの印象は、<br><br>

B社の社長は「世の中の役に立つことをしたい」という強い思いを持ったビジョナリーな起業家で、とても尊敬と共感ができる人物だと思いました。ただ、B社の展開するウェブサービスはすでに多くのユーザーを集めていましたが、当時はマネタイズがうまくいっていないという問題を抱えていました。<br><br>

　ユーザーから高い評価を受けているB社のサービスは、技術投資が先行し、お金を稼ぐ商品開発の部分に弱みがあったのです。<br><br>

Y氏は前職の会社がまだ未上場で30人規模の時代に入社し、会社の成長とともに自分自身も幅広く業務を担当して力をつけ、役員にまで昇進した人物です。<br><br>

　ところが諸事情で前職を退職することになり、今後のキャリアについて相談に来ました。そのとき、Y氏が考えていたのは「もう一度、ベンチャーに入ってその会社を成長させるような仕事をするか、あるいは自分で独立するか」でした。<br><br>

Y氏はビジネスマンとしての素養やモチベーションの高さに加え、人間力がありとても謙虚な人柄。これほどの人がマーケットに出てくる機会はめったにありません。</p>
        
