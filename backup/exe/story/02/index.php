         <div class="right_content_1 clear">
        		<p>そんなY氏に紹介するとしたらどんな会社がよいか。そこで浮かんだのがB社でした。しかし、当時のB社はまだ社員数20名程度の規模で、上場企業役員の転職先としてはまずあり得ない。そもそも、B社からオーダーを受けてもいなかったし、Y氏の給与水準も大きく下がる。<br><br>

確かにB社の社長から『こんな人が欲しい』といった話はまったく聞いていない状況だが、B社の抱えている課題とY氏のやりたいことや能力の高さをかけ合わせて考えると、こういうマッチングが面白いのではないか。そう考えて、われわれとしては堂々と両者に提案しました。<br><br>

　果たして、B社の社長とY氏を引き合わせる機会をセッティングすると、社長はY氏に一目ぼれし、Y氏も「面白そうだ」という印象を持たれ、その後、会食を通して二人は意気投合。他の役員とも会う機会を設け、最終的にY氏は入社を決断しました。<br><br>

　Y氏は入社後すぐ、全社員にインタビューを実施して現状の課題を抽出し、改革の方向性の立案と推進する役割を担うとともに、社内広報に注力して社長のビジョン浸透に努めました。もちろんY氏の力だけではないですが、そうした施策が奏功して売上が2倍近く成長するとともに、利益面も大きく改善されました。<br><br>

　常識的に考えるとありえないマッチングではありましたが、結果としてB社もY氏もハッピーな結果になりました。<br><br>

　一般的なヘッドハンターはクライアント企業の依頼を受けて動きますが、この案件に限らず「こんな凄い人がいます。御社に必要な人材ではないですか？」とわれわれからクライアントに提案するケースが多いのも弊社の特長です。そんなやり方が可能なのは、経営者とダイレクトに話をしているからだと考えます。<br><br>

ポジションがなく、仮に年俸3000万円の人材としても、数億円の利益を確保できるなら経営者としては採用に何の問題もないものです。社長の発想は「予算がなければつくればいい」。自分でリスクを背負っているから、そうした判断も社長ならできるのです。<br><br>
</p>
        
