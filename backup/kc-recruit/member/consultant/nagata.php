<div class="member-title">前職までの経歴</div>
<div class="member-text">アクセンチュアにて、コンサルタントとして、主に官公庁のお客様に対するプロジェクトのマネジメントや提案活動などに従事していました。</div>


<div class="member-title">なぜクライスに転職したのですか？</div>
<div class="member-text">アクセンチュアに在籍中、プロジェクトによって個人のパフォーマンスが大きく変わることを目の当たりにし、リーダー、職場環境、プロジェクト内容など様々な外部要因が個人のパフォーマンスに影響していると感じました。その時、個人にとってベストな環境を、プロジェクト単位ではなく会社単位で探すことができれば、社会全体の活性化につながるのではないかと考えたことが転職のきっかけです。
クライスのホームページを見たとき、会社全体で私が興味をもっているような仕事に真摯に向き合っていると感じ、社員の方々とお会いする中で、確信に変わったため、入社を決意いたしました。</div>

<div class="member-title">仕事をする中で一番のやりがいは何ですか？</div>
<div class="member-text">お客様である企業、候補者両者を深く理解し、積極的に働きかけていくことで、自分にしか為し得ないご縁が生まれることです。お会いさせていただく候補者の方の想いや考えを聞かせていただき、その方にとっての幸せを深く考えていくことで、候補者の方自身が想像し得なかったポジションをご提案できたり、新たな気づきに繋がることがあります。このような気づき、新たな可能性をご提案できた時に見られる候補者の方の表情が私の大きな原動力となります。</div>

<div class="member-title">今までで一番うれしかったできごとは何ですか？</div>
<div class="member-text">自分が担当している大好きな候補者の方や企業の期待に応えられ、喜んでいただけたとき、とても嬉しいです。また、候補者の方が、ご転職後、前向きな気持ちで働き、その結果、企業にも良い影響を及ぼすことができていると知った時には更に大きな喜びを感じます。<br>
先日、東証一部上場の大手小売メーカー(アパレル系)より、100社を超える人材紹介会社の中でリーダー～エグゼクティブの採用に最も貢献したエージェントとして表彰されました。私自身、その企業の理念にとても共感しており、ビジョンの実現に向けて何かお役に立てないかと考えていたため、受賞のお知らせを聞いた時には、この上ない喜びを感じました。</div>

<div class="member-title">今後の夢・目標を教えてください</div>
<div class="member-text">例えば、企業をどれだけ深く理解できているか。目の前にいらっしゃる候補者の方とどれだけお話できているか。素晴らしいご縁に関われるかどうかは自分のあり方次第です。自分にしかできない仕事を追求し、プロフェッショナルとして、日々高みを目指したいと考えています。</div>

<div class="member-title">応募者の皆さんへ一言</div>
<div class="member-text">入社して2,3年が経った頃から、この仕事のやりがいや奥深さを感じる瞬間が増えるようになりました。同時に、この仕事に携わる者に課せられる責任の重さや、仕事の難しさを日々感じています。私は、この仕事には「一生を賭けてやるだけの価値がある」と考えております。この想いに共感してくださり、少しでも興味をお持ちいただけましたら幸いです。</div>




