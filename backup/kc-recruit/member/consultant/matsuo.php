<div class="member-title">前職までの経歴</div>
<div class="member-text">IT企業にてSEからキャリアをスタートし、ローテーションで人事（採用）へ異動。人材採用に関する一連の業務、人材育成等の経験を経て、「個人のキャリアディベロップメント」に関心が高まり人材紹介会社へ転進を決意。IT、コンサル、企画系の職種を中心に主にIT企業の採用支援（営業担当）を行う。その後、よりハイクラスな方々のキャリア開発支援に関わることを志しクライス＆カンパニーに入社。</div>


<div class="member-title">なぜクライスに転職したのですか？</div>
<div class="member-text">「30代のキャリア支援」という事業ドメインがまさに自分自身が身を置きたいところと直感したためです。候補者オリエンティッドという考え方にも強い共感を覚えました。また、キャリアコンサルティング力を生涯磨き続けたいスキルと考えていた自分にとって、コンサルタント育成の仕組みやスキルアップへの考え方が、自己を成長させる場所として魅力的でした。社員の人格を磨き続けるという社風にも共感しました。</div>

<div class="member-title">仕事をする中で一番のやりがいは何ですか？</div>
<div class="member-text">自分が介在したからこそ生まれたご縁によって、候補者の方々の人生が良い方向に大きく変化していくこと、そして転職先の企業の業績や風土が大きく変化していくことです。この両者を目の当たりにした瞬間、大きなやりがいを感じます。候補者は一人の「人」。企業も経営者を中心とした「人」の集合体です。あらゆる人の『人生の転機』に向き合うことができることこそがこの仕事の醍醐味だと感じています。</div>

<div class="member-title">今までで一番うれしかったできごとは何ですか？</div>
<div class="member-text">一番は決められません(笑)。キャリアの相談、転職の支援、採用の支援、組織創りの助言など、私たちコンサルタントは日々色々な立場の方々の様々な課題に向き合い、解決の糸口をご提供しています。その全てが素晴らしい出会いであり一期一会であると考えています。そして、その方の期待に応えられたときには必ず感謝していただける仕事です。その感謝はいつも嬉しいです。日々喜びを感じているので、その大小はなく、順位を付けるのは難しいですね。</div>

<div class="member-title">今後の夢・目標を教えてください</div>
<div class="member-text">若年層(中学生・高校生)のキャリア教育の分野に進出したいと思っています。一人一人が若い頃から「自身の価値観」に素直に従い、自分らしく生きるための仕事選びがしやすい世の中を創ることが人生の目標です。『もっと自由に。もっと自分らしく。』キャリアコンサルタントとしてそんな多様な生き方を世の中に提案、発信できるようになることが目指す先です。</div>

<div class="member-title">応募者の皆さんへ一言</div>
<div class="member-text">人生の転機と向き合うキャリアコンサルタントという職業を真剣に追求したい方にとって、スキルや人格を磨くことに重きをおく弊社は非常に恵まれた環境であると感じます。自分が介在したからこそ生まれたご縁の重みをしっかりと感じたいと願う方と、共に切磋琢磨したいと心から願っています。</div>





